## 예정가격 질의답변

### Q1. 예정가격은 누가 작성하나요?

**A:** **계약담당자 또는 예정가격 작성 담당자**가 작성합니다.
- 원가계산이 필요한 공사: 설계부서에서 설계금액 산출 → 계약부서에서 예정가격 결정
- 물품·용역: 수요부서 사양 확정 → 계약부서에서 시장조사 후 결정
- 예정가격조서에 산출근거를 명확히 기재

---

### Q2. 복수예비가격은 어떻게 작동하나요?

**A:** 추정가격 **2억원 이상** 입찰에서 적용됩니다.
1. 기초금액의 ±3%(공사) 또는 ±5%(물품·용역) 범위에서 **15개** 가격 작성
2. 입찰참가자가 **4개 번호를 추첨**
3. 추첨된 4개의 **산술평균** = 예정가격
4. 전자입찰 시 시스템이 자동으로 처리

---

### Q3. 예정가격을 알려주면 처벌받나요?

**A:** **네, 형사처벌 대상입니다.**
- 공무원: **3년 이하 징역 또는 3천만원 이하 벌금**
- 기초금액, 복수예비가격 등 예정가격 추정 정보도 포함
