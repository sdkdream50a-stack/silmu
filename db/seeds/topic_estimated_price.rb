| **이윤** | 노무비+경비+일반관리비의 일정비율 |
| **부가가치세** | 공급가액의 10% |

### 제10조 (복수예비가격)

① 추정가격 **2억원 이상** 입찰에서 적용

② 절차:
1. **15개** 복수예비가격 작성
2. 입찰참가자가 **4개 추첨** (전자입찰 시 시스템 자동)
3. 추첨된 4개의 **산술평균** = 예정가격

③ 복수예비가격은 기초금액의 **±3%** 범위 내에서 작성 (공사)
