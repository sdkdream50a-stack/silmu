### Q2. 지수조정률과 품목조정률의 차이는?

**A:**
- **지수조정률:** 한국은행 물가지수를 기초로 비목별 가중평균 산정 → **간편하나 평균적** 반영
- **품목조정률:** 개별 품목의 실제 가격 변동을 직접 산정 → **정확하나 복잡**
- 계약상대자가 선택하며, 동일 계약에서 **방법 변경 불가**
- 미선택 시 지수조정률이 기본 적용

