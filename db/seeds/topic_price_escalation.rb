### Q3. 선금을 받은 경우 물가변동 조정에 어떤 영향이 있나요?

**A:** 선금 지급분은 **물가변동 조정에서 제외**됩니다.
- 조정금액 = 물가변동 조정금액 x (1 - 선금 비율)
- 예: 조정금액 5,000만원, 선금 30% → 실제 조정금액 3,500만원
- 이유: 선금은 이미 기존 단가로 지급된 금액이므로 이중 반영 방지

