### Q. 물가변동 조정과 설계변경 조정을 동시에 할 수 있나?

**A:** **가능합니다.** 물가변동 조정(제22조)과 설계변경 조정(제20조)은 별개의 사유에 의한 별개의 조정이므로 동시 적용이 가능합니다. 다만, 설계변경으로 증감된 금액은 설계변경 시점의 단가를 기준으로 하며, 물가변동 조정은 원래 계약금액 기준으로 산정합니다.
- 각 조정 사유별로 별도 산정
- 이중 조정(동일 사유 중복 반영)은 불가

---

### Q. 하도급 대금도 물가변동 조정 대상인가?

**A:** 원도급 계약금액이 물가변동으로 조정되면, 원수급자(원도급업체)는 하도급 대금도 **비율에 따라 조정해야 합니다.** 하도급거래 공정화에 관한 법률에서 원도급 계약금액 조정 시 하도급 대금의 연동 조정을 의무화하고 있습니다.
- 근거: 하도급법 제16조의2
- 조정 비율: 원도급 조정 비율에 상응
