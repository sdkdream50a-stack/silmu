| 재료비 | 3.5억원 | 35% | 100.0 | 106.0 | +6.0% |
| 경비 | 2.5억원 | 25% | 100.0 | 102.0 | +2.0% |

**가중평균 등락률:** (4.0 x 0.4) + (6.0 x 0.35) + (2.0 x 0.25) = 1.6 + 2.1 + 0.5 = **4.2%**

