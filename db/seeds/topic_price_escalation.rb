## 물가변동 관련 유권해석

### Q. 등락률 3% 미만이면 절대 조정할 수 없나?

**A:** **원칙적으로 조정할 수 없습니다.** 지방계약법 시행령 제65조에서 등락률 3% 이상을 조정 요건으로 규정하고 있으므로, 3% 미만의 변동은 계약당사자가 감수해야 합니다. 다만, 특수한 계약 조건(특약)으로 달리 정한 경우는 예외가 될 수 있습니다.
- 근거: 시행령 제65조 제1항
- 3% 기준은 사회적 통상의 물가 변동 범위를 고려한 것

---

### Q. 지수조정률 방식에서 품목조정률 방식으로 변경할 수 있나?

**A:** **동일 계약에서는 변경할 수 없습니다.** 최초 물가변동 조정 시 선택한 방법을 해당 계약 종료 시까지 유지해야 합니다. 다만, 계속비계약의 차수별 계약이나 장기계속계약의 연차별 계약에서 별도 계약으로 보는 경우에는 새로운 방법을 선택할 수 있다는 해석이 있습니다.
- 근거: 시행령 제65조 제7항
- 방법 선택은 신중하게 해야 함

