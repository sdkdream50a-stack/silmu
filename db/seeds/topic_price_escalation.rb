## 지방계약법 시행령 — 물가변동 관련 조항

### 제65조 (물가변동으로 인한 계약금액의 조정)

계약 체결일(조정 기준일이 있는 경우 그 기준일)로부터 **90일 이상 경과**하고, 다음 요건을 충족하면 계약금액을 조정한다:

| 조정 방법 | 조정 요건 (등락률) |
|----------|----------------|
| **지수조정률** | 입찰일(또는 직전 조정기준일) 기준 **등락률 3% 이상** |
| **품목조정률** | 입찰일(또는 직전 조정기준일) 기준 **등락률 3% 이상** |

### 조정 요건 정리

> **두 가지 요건을 모두 충족**해야 조정 가능:
> 1. 계약체결일(또는 직전 조정기준일)로부터 **90일 이상 경과**
> 2. 등락률이 **3% 이상** 변동

### 지수조정률 방식 (제65조 제2항)

한국은행이 조사·발표하는 **생산자물가지수** 또는 **수입물가지수** 등을 기초로 산정:

> **지수조정률 = 각 품목·비목의 등락률 x 해당 비목의 계약금액 비중**

**계산 공식:**

| 항목 | 산식 |
|------|------|
| 등락폭(K) | K = (조정기준일 지수 - 입찰일 지수) / 입찰일 지수 |
| 물가변동 조정금액 | 조정금액 = 계약금액 x K (각 비목별 가중평균) |

**특징:**
- 비목별 물가지수를 가중평균하여 산정
- 개별 품목 변동이 아닌 **비목군 전체의 변동률** 반영
- 산정이 비교적 간편하여 **가장 많이 사용**

### 품목조정률 방식 (제65조 제3항)

계약금액을 구성하는 **개별 품목·비목의 가격변동**을 직접 산정:

> **품목조정률 = 각 품목의 (변동 후 단가 - 변동 전 단가) x 수량의 합계 / 계약금액**

**특징:**
- 실제 품목별 가격 변동을 직접 반영
- **정확도가 높으나** 산정에 시간과 노력이 많이 소요
- 주요 자재의 가격 변동이 클 때 유리

### 조정 방법의 선택

- 지수조정률과 품목조정률 중 **계약상대자가 선택**
- 단, **동일 계약에서 조정 방법 변경 불가** (최초 선택 방법 유지)
- 선택하지 않은 경우: **지수조정률 적용**

### 제73조 (선금 지급분의 제외)

물가변동 조정 시 **선금 지급분은 조정 대상에서 제외**한다:

> 조정금액 = 물가변동 조정금액 x (1 - 선금 비율)

**예시:** 계약금액 10억원, 선금 30% 지급, 지수조정률 5%인 경우
- 물가변동 조정금액: 10억 x 5% = 5,000만원
- 선금 제외 후 조정금액: 5,000만원 x (1 - 0.3) = **3,500만원**
