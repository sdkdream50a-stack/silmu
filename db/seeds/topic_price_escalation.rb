## 물가변동 질의답변

### Q1. 물가변동 조정의 요건은 무엇인가요?

**A:** 두 가지 요건을 **모두 충족**해야 합니다.
- **기간 요건:** 계약체결일(또는 직전 조정기준일)로부터 **90일 이상 경과**
- **등락률 요건:** 등락률이 **3% 이상** 변동
- 두 요건 중 하나라도 미충족하면 조정 불가
- 물가 상승뿐 아니라 **하락** 시에도 적용

