## 지방자치단체 입찰 및 계약집행기준

### 물가변동 조정 업무 절차

**전체 흐름:**
조정 요건 확인 → 조정 방법 선택(지수/품목) → 조정률 산정 → 조정금액 산출 → 계약금액 변경

### 조정 기준일과 산출일

| 구분 | 의미 | 결정 방법 |
|------|------|----------|
| **조정기준일** | 물가변동 등락률을 산출하는 **기준 시점** | 계약상대자가 조정 신청한 날 (또는 발주기관이 정한 날) |
| **산출일** | 변동된 가격을 **실제 산출하는 시점** | 조정기준일 전일 기준으로 산출 |

### 지수조정률 산정 절차

1. **비목 분류:** 계약금액을 노무비·재료비·경비 등 비목별로 분류
2. **지수 확인:** 한국은행 생산자물가지수(또는 수입물가지수) 확인
3. **등락률 산정:** 각 비목별 (조정기준일 지수 - 입찰일 지수) / 입찰일 지수
4. **가중평균:** 비목별 등락률 x 비목별 금액 비중
5. **조정률 산출:** 가중평균 등락률이 3% 이상이면 조정률로 확정

### 품목조정률 산정 절차

1. **품목 확인:** 계약 내역서의 모든 품목 확인
2. **가격 조사:** 조정기준일 기준 각 품목의 거래실례가격 조사
3. **변동액 산정:** (변동 후 단가 - 변동 전 단가) x 수량
4. **합산:** 전체 품목의 변동액 합산
5. **조정률 확인:** 합산액 / 계약금액이 3% 이상이면 조정

### 조정 신청 및 처리 기한

- **조정 신청:** 계약상대자가 **조정기준일부터 30일 이내** 조정 신청
- **처리 기한:** 신청 접수 후 **30일 이내** 조정 여부 결정·통보
- **계약변경:** 조정 확정 후 변경계약 체결
