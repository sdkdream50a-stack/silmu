### Q1. 계약해제와 계약해지는 무엇이 다른가요?

**A:** 핵심 차이는 **소급효 여부**입니다.
- **해제:** 계약을 처음부터 없었던 것으로 합니다 (소급효). 원상회복 의무가 발생하며, 이미 납품된 물품은 반환해야 합니다.
- **해지:** 장래에 대해서만 효력이 소멸합니다 (장래효). 이미 이행된 부분(기시공분)은 유효하며, 대가를 정산합니다.
