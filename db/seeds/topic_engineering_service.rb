## 엔지니어링 용역 대가 기준

### 1. 공표 요율 방식 (실적공사비 방식)

공사비의 일정 비율로 엔지니어링 대가를 산정하는 방식:

> **엔지니어링 대가 = 공사비 × 공표 요율**

| 업무 구분 | 적용 대상 | 비고 |
|---------|---------|------|
| 기본설계 | 공사 설계 단계별 요율 적용 | 산업부 고시 별표 |
| 실시설계 | 기본설계 이후 상세 설계 | |
| 감리 | 시공 감독 및 품질 관리 | |
| 타당성 조사 | 사업 추진 여부 검토 | |

**특징:**
- 산정이 간편하고 객관적
- 업무 난이도·공사 규모에 따라 요율이 달라짐
- 소규모 공사보다 대규모 공사의 요율이 낮아지는 체감 구조

### 2. 실비정액 가산 방식

실제 투입되는 비용을 직접 산정하는 방식:

> **엔지니어링 대가 = 직접인건비 + 직접경비 + 제경비 + 기술료**

| 구성 항목 | 내용 | 산정 기준 |
|---------|------|---------|
| **직접인건비** | 투입 기술자의 인건비 | 엔지니어링 노임단가 x 투입 공수 |
| **직접경비** | 출장비·시험·검사비 등 | 실제 발생 비용 |
| **제경비** | 간접비 (임대료·보험료 등) | 직접인건비의 110~120% |
| **기술료** | 기술 개발·이익 요소 | (직접인건비+제경비)의 20~40% |

**특징:**
- 투입 인력·기간이 명확할 때 정확하게 산정 가능
- 대형 프로젝트·난이도 높은 과업에 적합
- 공수(man-month) 산정이 핵심

### 기술제안서 평가 방법

엔지니어링 용역 입찰 시 기술력을 평가하는 절차:

| 평가 항목 | 배점 | 주요 내용 |
|---------|------|---------|
| **기술 능력** | 40~60점 | 유사 용역 수행 실적, 핵심 기술자 경력 |
| **수행 계획** | 20~30점 | 과업 이해도, 수행 방법론 |
| **가격** | 20~30점 | 제안 금액의 적정성 |

- 기술평가 점수와 가격 점수를 합산하여 최고득점자를 낙찰자로 결정
- **입찰금액 비공개** 원칙: 기술평가 완료 후 가격 개봉 (기술·가격 분리 평가)

### 설계 변경 시 대가 조정

설계 용역 수행 중 설계 범위·내용이 변경된 경우:

- **발주기관 요구에 의한 변경:** 추가 과업 범위에 해당하는 대가를 **실비 가산** 또는 **요율 재산정**으로 조정
- **법령 변경으로 인한 추가 업무:** 기타변경 조정 (시행령 제75조) 준용
- **설계 오류로 인한 재작업:** 수급자 귀책 — 별도 대가 지급 불가, 수급자 부담
- 변경 전 **발주기관의 서면 승인** 필수 (무단 과업 변경 시 대가 청구 불인정)

### 준공도서·저작권 귀속 규정

| 구분 | 내용 |
|------|------|
| **준공도서** | 설계도면, 시방서, 수량산출서, 구조 계산서 등 — 준공 시 발주기관에 **원본 제출** |
| **저작권** | 설계도서의 저작권은 **발주기관에 귀속** (계약서에 명시) |
| **사용권** | 수급자(설계사)는 포트폴리오 등 비상업적 목적으로 참조 가능 |
| **2차 저작물** | 설계도서를 기반으로 한 변경·수정 작업 시 발주기관의 사전 동의 필요 |

> 실무상 계약서에 저작권 귀속 조항을 반드시 명시해야 분쟁 예방 가능

### 하자담보책임 범위 (설계 오류)

| 구분 | 내용 |
|------|------|
| **책임 범위** | 설계 오류로 인한 공사비 증가, 재시공 비용, 추가 감리비 등 |
| **책임 기간** | 준공 후 **2~5년** (용역 종류별 상이, 계약서에 명시) |
| **책임 한도** | 통상 계약금액 이내 (별도 협의 가능) |
| **면책 사유** | 발주기관의 부정확한 발주 조건 제시, 불가항력, 발주기관이 설계 오류를 인지하고도 시공 지시한 경우 |

**설계 오류 입증 절차:**
1. 시공 중 설계 오류 발견 → 즉시 발주기관에 서면 통지
2. 발주기관이 원인 조사 (설계사 귀책 여부 확인)
3. 설계사 귀책으로 확정 시 → 추가 비용 설계사에 청구
4. 설계사의 책임 범위 내에서 보상
