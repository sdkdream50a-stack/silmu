### Q. 전자입찰서 제출 후 마감 전에 수정·철회가 가능한가?

**A:** **가능합니다.** 입찰 마감시간 이전에는 기존 입찰서를 **철회하고 재제출**할 수 있습니다. 마감시간이 경과하면 철회 및 수정이 불가능하며, 제출된 입찰서 기준으로 개찰이 진행됩니다.
- 근거: 지방계약법 시행령 제16조의2
- 시스템에서 철회·재제출 이력이 모두 기록됨

