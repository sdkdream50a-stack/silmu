### Q. 나라장터 시스템 장애로 입찰서를 제출하지 못한 경우 구제 방법은?

**A:** 나라장터 시스템 장애가 **조달청에서 공식 확인**된 경우, 장애 시간만큼 **입찰 마감시간이 자동 연장**됩니다. 개별 PC나 네트워크 문제는 시스템 장애에 해당하지 않으므로, 입찰 참가자는 사전에 시스템 환경을 점검해야 합니다.
