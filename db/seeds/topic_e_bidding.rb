## 전자입찰 관련 유권해석

### Q. 추정가격 2천만원 이하인 경우 반드시 나라장터를 이용해야 하나?

**A:** **의무는 아닙니다.** 추정가격 2천만원 초과 시 나라장터 이용이 의무이며, 2천만원 이하는 발주기관의 재량으로 이용 여부를 결정할 수 있습니다. 다만, 투명성 확보를 위해 가급적 나라장터를 이용하도록 권장하고 있습니다.
