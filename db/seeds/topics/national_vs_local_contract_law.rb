    |------|-----------|-----------|
    | 정식 법령명 | 국가를 당사자로 하는 계약에 관한 법률 | 지방자치단체를 당사자로 하는 계약에 관한 법률 |
    | 적용기관 | 국가기관 (중앙부처·헌법기관) | 지방자치단체 (시·도·시·군·구, 교육청) |
    | 주무부처 | 기획재정부 | 행정안전부 |
    | 관련 시행령 | 국가를 당사자로 하는 계약에 관한 법률 시행령 | 지방자치단체를 당사자로 하는 계약에 관한 법률 시행령 |
