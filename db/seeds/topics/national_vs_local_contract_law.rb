    ## 국가계약법 제1조 (목적)

    **국가를 당사자로 하는 계약에 관한 법률 제1조 (목적)**

    <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
    이 법은 국가를 당사자로 하는 계약에 관한 기본적인 사항을 규정함으로써 계약업무를 원활하게 수행하게 함을 목적으로 한다.
    </div>

    ## 지방계약법 제1조 (목적)

    **지방자치단체를 당사자로 하는 계약에 관한 법률 제1조 (목적)**

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
    이 법은 지방자치단체를 당사자로 하는 계약에 관한 기본적인 사항을 규정함으로써 계약업무를 원활하게 수행하게 함을 목적으로 한다.
    </div>

    <div style="background:#eff6ff;padding:12px;border-radius:8px;margin:8px 0;">
    ■ 두 법의 적용 대상 기관 차이

    - **국가계약법**: 중앙행정기관(기획재정부·행정안전부·교육부 등), 그 소속 기관, 헌법기관(국회·법원·헌법재판소·중앙선거관리위원회), 감사원
    - **지방계약법**: 특별시·광역시·특별자치시·도·특별자치도, 시·군·자치구, 그 소속 기관(교육청 포함)

    국가기관인지 지방자치단체인지에 따라 적용 법령이 완전히 달라지므로, 자신의 소속 기관부터 정확히 파악해야 합니다.
    </div>

