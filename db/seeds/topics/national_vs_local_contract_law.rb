    ■ 실무 주의사항

    나라장터(G2B)를 통한 전자입찰에서는 발주기관이 국가기관인지 지방자치단체인지에 따라 시스템에서 자동으로 해당 서식을 제공합니다. 공고문에서 발주기관 유형을 반드시 확인하세요.
