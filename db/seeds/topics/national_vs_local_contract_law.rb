    ### 두 예규의 적용 범위 차이

    <div style="background:#fef3c7;padding:12px;border-radius:8px;margin:8px 0;">
    ⚠️ 예규는 법령(법률·시행령·시행규칙)보다 하위 규범이지만, 실무 계약 처리의 세부 기준을 규정합니다.

    - **기획재정부 예규**: 국가기관에만 적용. 지방자치단체는 적용 불가.
    - **행정안전부 예규**: 지방자치단체에만 적용. 국가기관은 해당 없음.
    - **혼용 금지**: 국가기관 담당자가 지방계약법 예규를 참고하거나, 지방자치단체 담당자가 기획재정부 예규를 적용하면 감사 지적 사유가 됩니다.
