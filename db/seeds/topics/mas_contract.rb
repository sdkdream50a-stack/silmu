    ## 조달사업에 관한 법률 제9조의2 (다수공급자계약)

    **조달사업에 관한 법률 제9조의2 (다수공급자계약)**

    <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
    ① 조달청장은 수요기관이 요구하는 물자를 효율적으로 공급하기 위하여 필요한 경우에는 품질·성능·효율 등에서 동등하거나 유사한 종류의 물자를 공급할 수 있는 둘 이상의 공급자와 계약(이하 "다수공급자계약"이라 한다)을 체결할 수 있다.

    ② 수요기관의 장은 제1항에 따른 다수공급자계약이 체결된 물자를 구매할 때에는 제1항에 따른 공급자 중에서 구매조건, 납품능력 등을 고려하여 최종 공급자를 선택하여야 한다.

    ③ 제1항 및 제2항에 따른 다수공급자계약의 계약 방법, 수요기관의 최종 공급자 선택 기준 및 절차 등에 필요한 사항은 기획재정부령으로 정한다.
    </div>

    **조달사업에 관한 법률 제2조 (정의)**

    <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
    이 법에서 사용하는 용어의 뜻은 다음과 같다.

    **"다수공급자계약"**이란 수요기관이 필요로 하는 규격이 표준화되어 있거나 상용화된 물자에 대하여 조달청장이 품질·성능·효율 등이 동등하거나 유사한 물자를 공급할 수 있는 둘 이상의 공급업체와 단가 및 그 밖의 거래조건을 정하여 체결하는 계약을 말한다.
    </div>

    <div style="background:#eff6ff;padding:12px;border-radius:8px;margin:8px 0;">
    ■ MAS의 법적 성격 및 위임 체계

    MAS(Multiple Award Schedule)는 **단가계약의 특수한 형태**입니다.

    - 일반 단가계약: 발주기관이 특정 공급자와 1:1로 단가 계약 체결
    - 다수공급자계약: 조달청이 다수 공급자와 동시에 단가 계약 체결 → 수요기관이 등록된 공급자 중에서 선택

    수요기관은 나라장터 종합쇼핑몰에서 MAS 등록 물품을 검색·비교하여 구매합니다. 별도의 입찰공고 없이 쇼핑몰에서 직접 선택하므로 조달 절차가 대폭 간소화됩니다.

    📌 **위임 체계**: 조달사업법 제9조의2 제3항 → **기획재정부령**(조달사업법 시행규칙)에서 계약 방법·최종 공급자 선택 기준 규정 → **다수공급자계약 업무처리규정**(조달청 고시)에서 세부 운영 절차 규정
    </div>

    <div style="background:#f3f4f6;padding:12px;border-radius:8px;margin:8px 0;">
    ※ 관련 법령: 조달사업에 관한 법률 제9조의2, 제2조 / 조달사업에 관한 법률 시행규칙(기획재정부령) / 다수공급자계약 업무처리규정(조달청 고시)
    </div>
