    ## 다수공급자계약 업무처리규정 (조달청 고시) 핵심 내용

    ### 조달청 고시 주요 규정

    <div style="background:#fef3c7;padding:12px;border-radius:8px;margin:8px 0;">
    **MAS 등록 심사 기준:**
    - 품질인증서 또는 성능 시험 결과 제출
    - 등록 상한가격 적정성 검토 (시중가격 대비)
    - 조달청이 정기적으로 가격 적정성 재검토

    **2단계 경쟁 방법:**
    1. **가격경쟁**: 등록업체 중 최저가 제시 업체 선택
    2. **가격·성능 혼합경쟁**: 가격 외 납품 기간, 유지보수 조건 등 종합 평가 후 선택

    **계약 유효기간:** 통상 3년 이내 (연장 가능, 최대 5년)
    </div>

    ### 나라장터 종합쇼핑몰 이용 절차

    <div style="background:#fef3c7;padding:12px;border-radius:8px;margin:8px 0;">
    **단계별 구매 절차:**

    1. **물품 검색** — 나라장터(G2B) 종합쇼핑몰에서 품목 검색
    2. **업체 선택** — 등록업체 목록에서 가격·납품조건 비교
       - 2천만원 이하: 1개 업체 직접 선택 가능
       - 2천만원 초과: 3개 이상 업체 견적 요청 필수
    3. **견적 요청** — 2단계 경쟁 대상인 경우 3개 이상 업체에 전자견적 요청
    4. **납품 요청** — 최종 업체 선정 후 납품요청서 발행 (계약서 역할)
    5. **검수/대금지급** — 납품 확인 후 검수조서 작성 → 대금 지급

    ※ G2B 시스템에서 전 과정이 전자적으로 처리됩니다.
    </div>

    ### 2단계 경쟁 방법 상세

    <div style="background:#fef3c7;padding:12px;border-radius:8px;margin:8px 0;">
    **가격경쟁:**
    - 3개 이상 등록업체에 전자견적 요청
    - 제출된 견적 중 최저가 업체 선정
    - 절차 단순, 규격이 동일한 물품에 적합

    **가격·성능 혼합경쟁:**
    - 가격 외 납품기간, A/S 조건, 성능 등 종합 평가
    - 평가표 작성 후 종합 점수 산정
    - IT 장비, 정밀기기 등 성능 차이가 중요한 물품에 적합
    </div>
