    ## MAS 계약 구조 및 2단계 경쟁

    **조달사업에 관한 법률 시행규칙 (기획재정부령) - MAS 계약 방법**

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
    조달사업법 제9조의2 제3항에 따라 기획재정부령(조달사업법 시행규칙)에서 다수공급자계약의 계약 방법, 수요기관의 최종 공급자 선택 기준 및 절차를 정합니다.

    ※ MAS는 조달청이 공개경쟁(규격·가격 심사)을 통해 다수의 적격 공급자를 등록하고, 수요기관이 등록 공급자 중에서 최종 공급자를 선택하는 2단계 구조입니다.

    ⚠️ 참고: 수요기관의 개별 구매 시 국가계약법의 경쟁입찰 원칙과의 관계에 대해서는 조달사업법이 특별법으로서 우선 적용됩니다.
    </div>

    ### MAS 2단계 계약 구조

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
    **1단계: 조달청 ↔ 다수 공급업체**

    - 조달청이 공급업체 신청을 받아 품질·가격 심사 후 등록
    - 등록된 업체와 단가계약 체결 (상한가격 협상 포함)
    - 나라장터 종합쇼핑몰에 물품·단가 등록
    - 계약 유효기간: 통상 1~3년 (연장 가능)

    **2단계: 수요기관 ↔ 최종 공급업체 선정**

    - 수요기관이 쇼핑몰에서 등록 물품 검색·비교
    - 2단계 경쟁 또는 최저가 선택으로 최종 공급자 결정
    - 납품 요청서 발행 → 납품 → 검수 → 대금 지급
    </div>

    ### 2단계 경쟁 의무 기준

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
    | 구매금액 | 2단계 경쟁 의무 여부 |
    |---------|-------------------|
    | **2천만원 이하** | 의무 아님 (최저가 선택 가능) |
    | **2천만원 초과** | **의무** — 3개 업체 이상 비교견적 (2단계 경쟁) |

    ※ 2천만원 초과 구매 시 반드시 3개 이상 등록 업체에 견적을 요청하고 비교·선택해야 합니다.

    ※ 근거: **다수공급자계약 업무처리규정(조달청 고시)**. 기준금액은 조달청 고시 개정에 따라 변경될 수 있으므로 최신 고시를 확인하시기 바랍니다.
    </div>

    ### MAS vs 일반입찰 비교

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
    | 구분 | MAS(다수공급자계약) | 일반입찰 |
    |-----|-------------------|---------|
    | 절차 | 쇼핑몰에서 선택 | 입찰공고 → 낙찰 |
    | 기간 | 1~3일 | 10일 이상 |
    | 적용 물품 | 규격 표준화 가능 물품 | 모든 물품·공사·용역 |
    | 예정가격 | 등록가격(상한가) 기준 | 별도 산정 |
    | 공급자 수 | 다수 등록업체 중 선택 | 입찰 참여업체 중 낙찰 |
    | 계약서 | 납품요청서로 대체 | 별도 계약서 작성 |
    | 적합 분야 | 사무용품·PC·소모품 등 | 공사·특수용역·대형 구매 |
    </div>

    ### 수요기관의 MAS 활용 기준 및 제한

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
    **활용 가능한 경우:**
    - 규격이 표준화되어 있는 물품 (사무용품, IT 기기, 소모품 등)
    - 긴급 구매가 필요한 경우
    - 반복적으로 소량 구매하는 물품

    **제한 사항:**
    - 공사, 특수 용역 등 규격 표준화가 어려운 계약은 MAS 대상 아님
    - 연간 구매 총액 관리 의무 (분할 구매 금지)
    - MAS 등록 가격 초과 구매 불가
    </div>
