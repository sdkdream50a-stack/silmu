    ## 나라장터(G2B) 전자조달시스템

    ### 나라장터 개요

    <div style="background:#f0fdf4;padding:12px;border-radius:8px;margin:8px 0;">
    **나라장터(G2B, Government to Business)**는 조달청이 운영하는 국가 전자조달시스템으로, 공공조달의 전 과정을 온라인으로 처리한다.

    - **운영기관**: 조달청
    - **접속 주소**: www.g2b.go.kr
    - **연간 처리 규모**: 약 180조원 (전체 공공조달의 70% 이상)
    </div>

    ### 나라장터에서 처리되는 업무

    | 업무 | 내용 |
    |------|------|
    | **입찰공고** | 수요기관이 구매 수요를 나라장터에 공고. 전국 단위 공개 |
    | **전자입찰** | 업체가 나라장터를 통해 전자적으로 입찰서 제출 |
    | **전자계약** | 낙찰 후 계약서를 전자 서명으로 체결 (종이 계약 대체) |
    | **대금지급** | 기성금·준공금 청구 및 지급 처리 |
    | **계약이행 관리** | 납품 확인, 검수 결과 등록, 하자보증 관리 |

    ### 나라장터 종합쇼핑몰 vs 일반 입찰 비교

    <div style="background:#fefce8;padding:12px;border-radius:8px;margin:8px 0;">

    | 구분 | 나라장터 종합쇼핑몰 | 일반 입찰 |
    |------|-------------------|----------|
    | **개념** | 미리 협상된 가격·규격의 제품을 쇼핑몰에서 즉시 구매 | 개별 발주마다 입찰 절차를 거쳐 공급자 선정 |
    | **절차** | 쇼핑몰 검색 → 주문 → 납품 (1~3일) | 공고 → 입찰 → 개찰 → 계약 (2주~수개월) |
    | **적합 품목** | 규격·가격이 표준화된 소모품·장비 | 맞춤 제작, 대형 공사, 전문 용역 |
    | **가격 결정** | 조달청이 사전에 계약한 단가 | 입찰 경쟁으로 결정 |
    | **장점** | 절차 간소, 신속 구매 | 경쟁으로 가격 인하 가능 |

