    ### 공공조달의 기본 원칙 5가지

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">

    | 원칙 | 내용 |
    |------|------|
    | **경쟁성** | 가능한 한 많은 공급자가 참여하는 공개경쟁을 원칙으로 하여 예산 절감과 품질 향상을 동시에 추구 |
    | **투명성** | 조달 과정(공고·심사·낙찰·계약)을 공개하여 외부 감시와 사후 감사가 가능하도록 함 |
    | **공정성** | 모든 공급자에게 동일한 기회와 기준을 적용하고, 특혜나 차별을 배제 |
    | **효율성** | 최소의 비용으로 최대의 가치(Value for Money)를 실현하고, 행정 절차를 간소화 |
    | **책임성** | 조달 결과에 대해 발주기관과 담당자가 명확한 책임을 지고, 감사·평가에 응함 |

    </div>

