    ## 공공조달 법령 체계

    ### 법령 위계 구조

    <div style="background:#fdf4ff;padding:12px;border-radius:8px;margin:8px 0;">

    ```
    법률 (국회 제정)
      └── 시행령 (대통령령)
            └── 시행규칙 (부령)
                  └── 예규·편람·고시 (행정규칙)
    ```

    법률은 기본 원칙과 권한을 규정하고, 세부 기준은 시행령·시행규칙·예규로 위임합니다.
    예규·편람은 법적 구속력이 약하지만, 실무에서 가장 자주 참조하는 기준입니다.

