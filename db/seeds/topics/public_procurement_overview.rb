    ## 공공조달의 법적 정의와 근거

    ### 조달사업법 제1조 (목적)

    <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
    이 법은 조달사업을 효율적으로 수행하기 위하여 필요한 사항을 규정함으로써 수요기관이 필요로 하는 물자의 적정한 가격과 품질을 보장하고 조달절차의 공정성과 투명성을 높여 국가재정의 효율적인 운영에 이바지함을 목적으로 한다.
    </div>

    ### 조달사업법 제2조 (정의)

    <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
    이 법에서 사용하는 용어의 뜻은 다음과 같다.

    1. **"조달물자"**란 수요기관이 그 업무에 사용하기 위하여 구매·제조·가공·수리·임차하거나 설치·시공하는 물자 및 시설공사를 말한다.
    2. **"수요기관"**이란 조달청에 조달을 요청하는 중앙관서 및 그 소속기관, 지방자치단체, 공공기관 등을 말한다.

    ※ 조달청은 정부조직법 제39조에 근거한 기획재정부 소속 외청으로, 조달사업법 제3조에 따라 조달사업을 총괄·수행합니다.
    </div>

    <div style="background:#eff6ff;padding:12px;border-radius:8px;margin:8px 0;">
    ■ 공공조달의 정의와 범위

    **공공조달**이란 공공기관(국가기관·지방자치단체·공공기관 등)이 행정 목적 수행을 위하여 민간 시장에서 **재화(물품), 용역, 공사를 구매·조달**하는 일련의 행위를 말한다.

    - **물품 조달**: 소모품, 장비, 비품 등 유형의 재화 구매
    - **용역 조달**: 청소, 경비, 컨설팅, IT 등 서비스 구매
    - **공사 조달**: 건축, 도로, 시설 등 건설공사 발주
    </div>

    ### 국가계약법 제5조 (계약의 원칙)

    <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
    각 중앙관서의 장 또는 계약담당공무원은 계약을 체결함에 있어서 서로 대등한 입장에서 당사자의 합의에 따라 체결하여야 하며, 당사자는 계약의 내용을 신의성실의 원칙에 따라 이행하여야 한다.

    ※ 국가계약법은 대등원칙·신의성실을 기본 계약원칙으로 선언하고, 구체적인 경쟁·공정·투명성 확보 방안은 시행령 이하에서 규율합니다.
    </div>

    ### 지방계약법 제6조 (계약의 원칙)

    <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
    ① 지방자치단체를 당사자로 하는 계약에 관한 사무를 처리할 때에는 계약의 공정성·투명성·경쟁성을 확보하고, 지역경제 발전에 이바지함을 원칙으로 한다.

    ② 계약담당자는 법령을 준수하고, 예산의 적정한 집행을 위하여 노력하여야 한다.
    </div>

