    A. 용역비는 계약서를 체결하고 서비스를 제공받는 경우(청소용역, IT컨설팅 등), 일반수용비는 물품 구매·소규모 서비스 등 계약 없이 처리하는 경우입니다. 단가계약이나 장기 서비스라면 용역비로 처리해야 합니다.

