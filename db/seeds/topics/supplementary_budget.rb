    **Q. 추경은 몇 번까지 편성할 수 있나요?**
    A. 법적으로 횟수 제한이 없습니다. 다만 재정 관리 효율성을 위해 대부분의 자치단체는 연 2~3회를 원칙으로 운영합니다. 매번 의회 의결이 필요하므로 횟수가 많을수록 행정 부담이 증가합니다.

