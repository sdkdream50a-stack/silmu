## 자주 묻는 질문 (FAQ)

**Q1. 95점 넘었는데 왜 2등인가요?**

A: 적격심사는 최저가 투찰자부터 순서대로 심사하여, 95점 이상인 첫 번째 업체가 낙찰됩니다. 가격이 낮을수록 먼저 심사받으므로, 점수가 높아도 가격이 높으면 심사 기회조차 없을 수 있습니다.

**Q2. 서류 1개 누락하면 탈락인가요?**

A: 필수 서류 누락 시 해당 항목 0점 처리로 95점 통과가 거의 불가능합니다. 제출 전 체크리스트 필수.

**Q3. 지역업체 가점은 필수인가요?**

A: 아닙니다. 발주기관이 선택적으로 설정하며, 공고문에 명시된 경우에만 가점 적용됩니다.

**Q4. 하도급 실적도 인정되나요?**

A: 원칙적으로 원도급 실적만 인정됩니다. (단, 일부 공고에서 하도급 실적 인정하는 경우도 있으니 공고문 확인 필수)
