    - 보조금 교부 신청서에는 사업계획서, 수지예산서, 자부담 확보 계획 등을 첨부하여 중앙관서의 장에게 제출
    - 중앙관서의 장은 신청 내용을 심사하여 교부 여부·금액·조건 등을 결정

