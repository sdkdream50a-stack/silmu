    **Q. 보조율은 어떻게 정해지나요?**
    A. 보조율은 중앙관서에서 사업별·지역별로 고시합니다. 농어촌 지역이나 재정자립도가 낮은 지역은 더 높은 보조율이 적용됩니다. 지방비(자부담) 비율은 보조율에 따라 달라지며, 보조율 50%이면 지방비 50%를 별도로 편성해야 합니다.

