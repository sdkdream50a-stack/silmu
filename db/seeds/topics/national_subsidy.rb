    <strong>제40조 (벌칙)</strong>
    거짓 신청이나 그 밖의 부정한 방법으로 보조금을 교부받거나 간접보조금을 교부받은 자는 10년 이하의 징역 또는 1억원 이하의 벌금에 처한다.
