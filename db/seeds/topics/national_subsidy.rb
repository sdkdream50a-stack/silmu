    A. 국고보조금 통합관리시스템(e나라도움, npas.go.kr)으로, 보조금 신청·교부·집행·정산 전 과정을 온라인으로 관리하는 정부 시스템입니다. 대부분의 국고보조사업은 이 시스템을 통해 처리됩니다.

