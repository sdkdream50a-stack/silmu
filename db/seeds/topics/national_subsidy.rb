    A. 사업 완료 후 집행 잔액은 원칙적으로 중앙관서에 반환해야 합니다. 중앙관서의 허가를 받은 경우에 한해 다음 연도로 이월하여 사용할 수 있습니다. 무단으로 보유하거나 다른 용도에 사용하면 환수 대상입니다.

