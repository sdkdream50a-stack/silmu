    ## 원가계산의 3가지 유형 비교

    공공계약에서 원가계산은 계약 목적물의 성격에 따라 **제조원가**, **용역원가**, **공사원가** 3가지로 구분됩니다.

    <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
    | 구분 | 제조원가 | 용역원가 | 공사원가 |
    |-----|---------|---------|---------|
    | 직접비 | 직접재료비, 직접노무비, 직접경비 | 직접인건비, 직접경비 | 재료비, 노무비, 경비 |
    | 간접비 | 간접재료비, 간접노무비, 간접경비 | 제경비 | 일반관리비 |
    | 이윤 | 이윤(25% 이내) | 기술료 | 이윤(15% 이내) |
    | 부가세 | VAT 10% | VAT 10% | VAT 10% |
    </div>

