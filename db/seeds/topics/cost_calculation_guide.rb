    ## 실무 요율표

    공공계약 원가계산 시 자주 사용하는 기준 요율입니다. **실제 계약 시에는 해당 연도 최신 예규 기준 요율을 반드시 확인하세요.**

    <div style="background:#f3e8ff;padding:12px;border-radius:8px;margin:8px 0;">
    | 비목 | 적용 기준 | 요율 범위 | 비고 |
    |------|----------|---------|------|
    | 제경비 (용역) | 직접인건비 대비 | 110~120% | 용역원가에만 적용 |
    | 기술료 (용역) | (직접인건비+제경비) 대비 | 20~40% | 이윤 대체 개념 |
    | 일반관리비 (제조) | 제조원가 대비 | 5~15% | 업종·규모에 따라 상이 |
    | 일반관리비 (공사) | 공사원가 대비 | 4~6% | 공사 종류·규모별 차등 |
    | 이윤 (제조) | (노무비+경비+일반관리비) 대비 | 25% 이내 | 상한 초과 불가 |
    | 이윤 (공사) | (노무비+경비+일반관리비) 대비 | 15% 이내 | 상한 초과 불가 |
    | 부가가치세 | 공급가액 대비 | 10% | 면세 용역 제외 |
