    ## 원가계산서 작성 요령

    <div style="background:#fef9c3;padding:12px;border-radius:8px;margin:8px 0;">
    원가계산서는 다음 항목을 순서대로 산출하여 작성합니다.

    | 순서 | 항목 | 작성 방법 |
    |------|------|----------|
    | 1 | 재료비 (직접재료비) | 품명·수량·단가·금액 명시 |
    | 2 | 노무비 | 직종·인원·노임단가·기간 명시 |
    | 3 | 경비 | 항목별 금액 (공통비 배분 근거 포함) |
    | 4 | 소계 (직접원가) | 재료비+노무비+경비 |
    | 5 | 일반관리비 | 소계 × 요율 |
    | 6 | 이윤 | (노무비+경비+일반관리비) × 요율 |
    | 7 | 합계 (공급가액) | 1~6 합산 |
    | 8 | 부가가치세 | 합계 × 10% |
    | 9 | 예정가격 | 합계 + 부가가치세 |
    </div>

