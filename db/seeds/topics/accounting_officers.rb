    A. 감사원이나 지방의회로부터 변상 판정을 받으면 손해액을 본인이 배상해야 합니다. 변상 판정에 불복하는 경우 심사청구·행정소송이 가능합니다. 납부하지 않으면 급여에서 공제될 수 있습니다.

