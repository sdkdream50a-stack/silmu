    **Q. 상급자 지시로 위법한 지출을 했을 때 담당자에게 책임이 있나요?**
    A. 명백히 위법한 지시임을 알면서도 따랐다면 담당자도 책임을 질 수 있습니다. 위법한 지시를 받으면 서면으로 이의를 제기하거나 상급자에게 재검토를 요청하고, 이 과정을 기록으로 남기는 것이 중요합니다.

