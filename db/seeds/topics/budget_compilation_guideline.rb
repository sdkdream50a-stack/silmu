    **Q. 행안부 예산편성 지침은 언제 나오나요?**
    A. 매년 9월~10월 행안부에서 시·도로 시달하고, 시·도가 다시 시·군·구로 전달합니다. 10월 말이면 대부분 기관에서 내용을 확인할 수 있습니다.

