## 하자보증 관련 유권해석

### Q. 하자보증기간이 지나면 하자 책임이 완전히 소멸하나?

**A:** 지방계약법상 하자보증기간이 만료되면 **하자보증금 반환 의무**가 발생합니다. 다만, 민법상 하자담보책임(10년)은 별도이므로, 중대한 하자에 대해서는 민법에 따라 별도 손해배상 청구가 가능할 수 있습니다.
- 지방계약법: 공종별 1~5년
- 민법: 건물 등 토지공작물은 10년

---

### Q. 물품·용역 계약에도 하자보증이 적용되나?

**A:** 하자보증은 주로 **공사계약**에 적용됩니다. 물품의 경우 품질보증기간으로, 용역의 경우 계약 조건에 따라 별도 약정합니다. 다만, 설치를 포함한 물품 납품 등에서는 하자보증을 적용할 수 있습니다.

---

### Q. 공종별 하자보증기간이 다른데 보증서를 하나로 제출할 수 있나?

**A:** **가능합니다.** 다만, 보증서의 유효기간은 **가장 긴 공종의 보증기간**(구조체 5년) 이상이어야 합니다. 또는 공종별로 **별도 보증서를 제출**하고 해당 공종 기간 만료 시 개별 반환받을 수 있습니다.

---

### Q. 하자보증기간 중 시설물 용도 변경 시 하자보증은?

**A:** 발주기관의 용도 변경으로 인한 하자는 **계약상대자의 하자보수 의무에서 제외**됩니다. 원래 설계·시방서대로 시공했는데 용도 변경으로 발생한 문제는 발주기관 귀책사유입니다.
