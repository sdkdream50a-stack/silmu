## 하자보증 질의답변

### Q1. 하자보증기간은 공종별로 어떻게 다른가요?

**A:** 공종별로 **1년~5년**까지 차등 적용됩니다.
- 구조체 (골조·기초): **5년**
- 지붕·방수·도로포장: **3년**
- 창호·전기·소방·설비: **2년**
- 도장·미장·타일: **1년**
- 기산일: **준공검사 합격일**

---

### Q2. 하자보증금은 얼마를 내야 하나요?

**A:** 공종별로 **계약금액의 2~5%** 범위에서 납부합니다.
- 구조체: **5%**
- 지붕·방수·도로: **3~4%**
- 창호·전기·설비: **3%**
