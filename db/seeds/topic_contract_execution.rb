## 계약체결 관련 유권해석

### Q. 낙찰자가 10일 이내 계약하지 않으면?

**A:** 정당한 사유 없이 10일 이내 계약을 체결하지 않으면 **부정당업자 제재** 대상이 됩니다. 차순위자에게 계약 체결을 요청할 수 있습니다.
- 근거: 시행령 제49조의2

---

### Q. 계약서 작성 생략 시 어떤 서류로 대체하나?

**A:** 계약서를 생략하더라도 다음 서류를 구비해야 합니다:
- **청구서** 또는 **납품서**
- **견적서** (가격 확인용)
- **수의계약 사유서**
- 이들 서류로 계약 내용 확인이 가능해야 함

---

### Q. 계약변경으로 금액이 증가하면 추가 보증금이 필요한가?

**A:** **네.** 계약금액이 증가하면 증가분에 대한 계약보증금을 **추가 납부**해야 합니다. 감소하면 차액을 반환합니다.

---

### Q. 계약해제와 계약해지의 차이는?

**A:**
- **계약해제:** 계약의 효력을 처음부터 소멸 (소급효) → 원상회복 의무
- **계약해지:** 장래에 대해서만 효력 소멸 → 기이행 부분은 유효
- 공사 계약 중도 해지 시: 기성 부분 정산 후 해지
