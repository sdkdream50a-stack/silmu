## 지방계약법 시행령 — 계약 체결 관련 조항

### 제49조 (계약서의 작성)

계약서에 기재해야 할 사항:
1. 계약의 **목적**
2. **계약금액**
3. **이행기간**
4. 계약보증금
5. 위험부담
6. 지체상금
7. 계약불이행 시 조치사항
8. 기타 필요한 사항

### 제50조 (계약서 작성 생략)

다음 경우 계약서 작성 생략 가능:
- 추정가격 **5천만원 이하** (물품·용역)
- 추정가격 **1억원 이하** (공사)
- 전기·가스·수도 등의 공급계약
- 경매에 부치는 경우

※ 계약서 생략 시에도 **청구서·납품서 등 증빙** 보관 필수

### 제49조의2 (계약 체결 기한)

- 낙찰통지를 받은 날부터 **10일 이내** 계약 체결
- 정당한 사유 없이 기한 내 미체결 → 부정당업자 제재 가능
- 발주기관 사유로 지연 시 → 지연일수만큼 이행기간 연장

### 제65조 (계약의 변경)

| 변경 사유 | 내용 |
|----------|------|
| 설계변경 | 공사 설계 변경에 따른 계약금액 조정 |
| 물가변동 | 물가변동(3% 이상)에 따른 계약금액 조정 |
| 기타 변경 | 계약 조건, 이행기간 변경 등 |

### 제67조 (계약의 해제·해지)

계약 해제·해지 사유:
- 계약상대자의 **계약 불이행**
- 부정당업자 **제재** 사유 발생
- 계약상대자의 **파산, 부도**
- 정당한 사유 없는 **공사 중단** (30일 이상)
