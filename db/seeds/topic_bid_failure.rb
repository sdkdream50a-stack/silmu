## 유찰 후 수의계약 관련 유권해석

### Q. 1인 입찰 유찰 시 반드시 해당 1인 업체와 수의계약해야 하나?

**A:** 반드시 해당 1인 업체와 계약해야 하는 것은 아닙니다. 1인 입찰로 유찰된 경우 수의계약으로 전환할 수 있으며, 이때 **2인 이상 견적을 다시 징구**하여 더 유리한 조건의 업체를 선택할 수 있습니다. 다만, 공급 가능한 업체가 해당 1인뿐임이 명확한 경우에는 해당 업체와 협의 진행이 가능합니다.
- 근거: 시행령 제26조 제1항

---

### Q. 2회 유찰 후 수의계약 전환 시 예정가격 범위를 초과하여 계약할 수 있나?

**A:** 원칙적으로 예정가격을 상한선으로 하여 계약금액을 결정해야 합니다. 유찰 사유가 예정가격의 현실성 부족이라면, **예정가격을 재산정**하는 방법을 검토하세요. 예정가격을 초과한 계약은 부정당 지출로 감사 지적 대상이 됩니다.
- 근거: 시행령 제89조 (예정가격의 효력)

---

### Q. 유찰 후 수의계약에서 견적업체를 선정하는 기준은?

**A:** 수의계약으로 전환 후에도 **2인 이상 견적을 징구**하는 것이 원칙입니다. 견적업체 선정 시 해당 물품·공사·용역을 실제로 수행할 수 있는 자격(면허·등록 등)을 갖춘 업체를 선정해야 합니다. 계약담당자는 특정 업체에 편향되지 않도록 주의해야 합니다.
- 근거: 시행규칙 제27조

---

### Q. 재공고 기간 중에도 수의계약 전환이 가능한가?

**A:** 재공고 기간이 아직 진행 중이라면 수의계약으로 전환할 수 없습니다. 재공고 입찰이 마감되고 2차 유찰이 확정된 후에야 수의계약으로 전환할 수 있습니다. 긴급한 경우라도 이 절차를 생략할 수 없으며, 긴급의 경우 별도 요건(시행령 제25조 제1항 제1호·제2호 — 천재지변·재난 긴급복구)을 검토해야 합니다.
