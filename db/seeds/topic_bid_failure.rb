## 유찰 후 수의계약 질의답변

### Q1. 유찰 후 수의계약으로 전환하려면 몇 번 유찰되어야 하나요?

**A:** 원칙적으로 **2회 입찰 유찰** 후 수의계약으로 전환할 수 있습니다. 단, 다음 경우는 1회 유찰 후 즉시 수의계약이 가능합니다.
- 입찰자가 **1인뿐**인 경우
- 기타 법령에서 정한 특례 요건 해당 시

---

### Q2. 재공고 시 입찰 조건을 변경할 수 있나요?

**A:** 재공고 시 입찰참가 자격 일부 완화, 공고기간 단축(5일 이상), 입찰 방법 변경 등은 가능합니다. 그러나 계약 목적물의 규격, 설계서·내역서의 핵심 내용은 변경할 수 없습니다. 예정가격은 유찰 사유에 따라 재산정이 가능합니다.

---

### Q3. 수의계약 전환 후 반드시 2인 이상 견적을 받아야 하나요?

**A:** 네. 수의계약으로 전환한 후에도 **2인 이상 견적을 징구**하는 것이 원칙입니다. 공급 가능한 업체가 1인뿐이라면 그 사유를 문서로 확인한 후 1인 견적으로 진행할 수 있습니다.

---

### Q4. 유찰 후 수의계약 전환 절차에서 결재는 어떻게 받나요?

**A:** 수의계약 전환 결재 시 ①유찰 경위서, ②수의계약 사유서(법적 근거 명시), ③견적 징구 계획 등을 포함하여 계약담당자 및 상급자의 결재를 받습니다. 기관에 따라 재무관·계약관 등 결재 라인이 다를 수 있으므로 내부 규정을 확인하세요.
