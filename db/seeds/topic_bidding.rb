## 지방계약법 제9조 (계약의 방법)

① 지방자치단체의 장 또는 계약담당자는 계약을 체결하려는 경우에는 **일반경쟁입찰에 부쳐야 한다.**

② 계약의 목적·성질·규모 및 지역특수성 등을 고려하여 필요하다고 인정되면 **대통령령으로 정하는 바에 따라** 참가자격을 제한하거나 참가자를 지명하여 경쟁에 부치거나 수의계약을 할 수 있다.

### 입찰의 법률적 위치

- **원칙:** 모든 계약은 **일반경쟁입찰** (제9조 제1항)
- **예외 1:** 제한경쟁·지명경쟁 (제9조 제2항 → 시행령 제13~24조)
- **예외 2:** 수의계약 (제9조 제2항 → 시행령 제25~26조)

> 국가계약법 제7조도 동일한 취지로 규정하며, 일반경쟁입찰 원칙을 명시

### 관련 조항

- **제9조의2 (입찰 무효):** 입찰참가자격이 없는 자가 한 입찰 등은 무효
