## 입찰 관련 유권해석

### Q. 2회 유찰 후 수의계약 시 조건 변경이 가능한가?

**A:** 원칙적으로 **최초 입찰에 부친 내용과 동일한 조건**으로 수의계약해야 합니다. 다만, 예정가격 이내에서 가격 협상은 가능합니다.
- 근거: 시행령 제25조 제1항 제5호
- 규격·수량 변경은 불가, 가격만 협상 가능

---

### Q. 입찰공고 기간 산정 시 공휴일을 포함하나?

**A:** **네, 역일(曆日) 기준**으로 산정합니다. 공고일과 입찰일을 제외하고 그 사이 기간이 법정 최소기간 이상이어야 합니다.
- 예: 7일 공고 → 공고일 다음날부터 7일 후 입찰

---

### Q. 입찰참가자격 제한 시 실적증명은 어떤 것을 인정하나?

**A:** 계약이행 실적은 **계약상대자로서 직접 이행한 실적**만 인정합니다. 하도급 실적은 원칙적으로 불인정하나, 일부 공사에서 예외 적용됩니다.

---

### Q. 전자입찰에서 입찰서 제출 후 취소가 가능한가?

**A:** 입찰 마감시간 **이전**에는 기존 입찰서를 철회하고 재제출 가능합니다. 마감 이후에는 **철회 불가**하며, 개찰 결과에 따라 처리됩니다.
