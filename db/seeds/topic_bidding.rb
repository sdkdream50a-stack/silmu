### 제15조 (지명경쟁입찰)

지명경쟁은 다음 경우에 한정:
- 계약의 성질·목적상 특수한 기술이 필요한 경우
- 추정가격 5천만원 이하로 입찰 대상자가 10인 이내인 경우
