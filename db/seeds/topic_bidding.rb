## 지방계약법 시행령 — 입찰 관련 조항

### 제13조 (일반경쟁입찰 참가자격)

입찰에 참가하려는 자는 다음 요건을 갖추어야 한다:
1. 해당 업종에 관한 **면허·허가·등록** 등의 요건을 갖출 것
2. **보안측정** 등 필요한 인증을 받을 것
3. **부정당업자 제재** 기간 중이 아닐 것

### 제14조 (제한경쟁입찰)

| 제한 사유 | 내용 |
|----------|------|
| 실적 제한 | 동종·유사 계약 이행 실적 |
