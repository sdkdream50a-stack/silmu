## 입찰 질의답변

### Q1. 입찰에 1인만 참가하면 어떻게 되나요?

**A:** 유찰 처리되고 **재공고입찰**을 실시합니다.
- 재공고에도 1인만 참가 → 그 1인과 **수의계약** 가능
- 조건: 최초 입찰과 동일한 조건, 예정가격 이내

---

### Q2. 낙찰자가 계약을 포기하면 어떻게 하나요?

**A:**
- **1순위:** 차순위 적격자에게 계약 체결 통보
- **2순위:** 차순위자도 포기 시 재입찰
- **포기자:** 정당한 사유 없으면 부정당업자 제재 검토
- 근거: 시행령 제49조, 제50조

---

### Q3. 예정가격을 초과한 입찰은 어떻게 처리하나요?

**A:** 예정가격을 초과한 입찰서는 **무효** 처리됩니다. 모든 입찰자가 예정가격을 초과한 경우 유찰 처리되며, 예정가격을 재산정하여 재입찰합니다.

---

### Q4. 전자입찰과 오프라인 입찰의 차이는?

**A:** 추정가격 **2천만원 초과** 시 **나라장터 전자입찰이 의무**입니다.
- 전자입찰: 나라장터(G2B)에서 온라인 제출
- 오프라인 입찰: 2천만원 이하만 가능
- 전자입찰 시스템 장애 시 예외적 서면입찰 가능
