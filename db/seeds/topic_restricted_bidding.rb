## 제한경쟁입찰 관련 유권해석

### Q. 지역 제한 적용 기준이 추정가격인가, 계약예정금액인가?

**A:** **추정가격** 기준입니다. 지방계약법 시행령 제20조는 '추정가격'을 기준으로 지역 제한 가능 여부를 판단합니다. 계약예정금액(예정가격)과 혼동하지 않도록 주의하세요.

---

### Q. 실적 제한 시 하도급 실적도 인정되나?

**A:** 원칙적으로 **불인정**입니다. 계약이행 실적은 계약상대자(원도급자)로서 직접 이행한 실적이어야 합니다. 다만, 공사의 경우 일부 전문공종에서 하도급 실적을 인정하는 예외가 있으니 해당 사업의 특성을 검토하여 결정하세요.

---

### Q. 제한 기준을 당해 계약금액과 동일하게 설정해도 되나?

**A:** **원칙적으로 금지**됩니다. 실적 기준을 당해 계약금액과 같거나 그 이상으로 설정하면 참가 가능한 업체가 지나치게 좁아져 과도한 제한으로 지적받을 수 있습니다. 통상 당해 계약금액의 50~70% 이하 수준에서 설정하는 것이 일반적입니다.

