### 제한 기준 설정 시 주의사항 (과도한 제한 금지)

1. 제한 기준은 계약 목적 달성에 **필요 최소한**으로 설정해야 합니다.
2. 특정 업체에만 유리하도록 제한 기준을 설정하면 **감사 지적·입찰 무효** 사유가 됩니다.
3. 실적 기준을 **당해 계약금액과 동일 또는 그 이상**으로 설정하는 것은 원칙적으로 금지입니다.
4. 복수의 제한 조건을 동시에 적용하려면 각 조건의 **적법성을 별도 검토**해야 합니다.
5. 제한 기준 설정 근거를 **문서로 보존**해야 합니다 (감사 대비).
