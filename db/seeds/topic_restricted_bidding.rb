## 지방계약법 제9조 (경쟁입찰의 원칙)

① 지방자치단체의 장 또는 계약담당자는 계약을 체결하려는 경우에는 **일반경쟁입찰에 부쳐야 한다.**

② 계약의 목적·성질·규모 및 지역특수성 등을 고려하여 필요하다고 인정되면 **대통령령으로 정하는 바에 따라** 참가자격을 제한하는 경쟁에 부칠 수 있다.

### 제한경쟁의 법률적 위치

- **원칙:** 모든 계약은 **일반경쟁입찰** (제9조 제1항)
- **예외:** 제한경쟁은 계약 목적 달성에 필요한 경우에 **한정적으로 허용** (제9조 제2항)
- **세부기준:** 시행령 제20조에서 허용 사유와 기준을 구체적으로 규정

> 제한경쟁은 경쟁의 원칙에 대한 예외이므로, 시행령이 정한 사유에 해당하지 않거나 과도하게 제한기준을 설정하면 위법·부당한 입찰이 됩니다.

