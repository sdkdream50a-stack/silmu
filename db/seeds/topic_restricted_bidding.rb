## 지방계약법 시행령 제20조 (제한경쟁입찰에 관한 기준)

지방자치단체의 장 또는 계약담당자는 법 제9조 제2항에 따라 제한경쟁에 부치는 경우에는 다음 각 호의 기준에 따라야 한다.

### 1. 실적 제한

계약이행의 **유사 실적**이 있는 자로 제한할 수 있다.

- 동종·유사한 계약의 이행 실적이 있는 업체로 참가자격 제한
- 실적 기준은 **계약 목적 달성에 필요한 최소한**으로 설정해야 함
- 과거 일정 기간(통상 최근 5년) 이내의 실적만 인정

### 2. 기술 능력 제한

특수한 **기술·설비·자재** 등이 필요한 경우 해당 능력을 보유한 자로 제한할 수 있다.

- 계약 이행에 특수한 기술·장비 또는 특정 자재가 필요한 경우
- 특허권, 실용신안권 등 지식재산권이 있는 경우
- 제조업체 직납이 필요한 경우

### 3. 지역 제한

**지역경제 활성화**를 위해 일정 추정가격 이하인 경우 해당 지역 소재 업체로 제한할 수 있다.

| 계약 종류 | 지역 제한 가능 추정가격 기준 |
|----------|--------------------------|
| 공사 (종합공사) | **추정가격 100억원 미만** |
| 공사 (전문·기타공사) | **추정가격 10억원 미만** |
| 물품(제조·구매) | **추정가격 5억원 미만** |
| 용역 | **추정가격 5억원 미만** |

- '해당 지역'은 해당 지방자치단체의 관할구역을 원칙으로 함
- 관할구역 내 적격업체가 없는 경우 광역시·도 단위로 확대 가능

### 4. 중소기업자 간 제한경쟁

「중소기업제품 구매촉진 및 판로지원에 관한 법률」에 따라 **중소기업자 간 제한경쟁**을 실시할 수 있다.

- 중소벤처기업부장관이 지정·고시한 **직접생산 확인품목**은 중소기업자 간 제한경쟁 의무
- 직접생산 확인 대상 품목 여부는 나라장터 또는 중소벤처기업부 홈페이지에서 확인
