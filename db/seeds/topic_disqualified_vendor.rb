## 지방계약법 제31조 (부정당업자의 입찰 참가자격 제한)

① 지방자치단체의 장 또는 계약담당자는 다음 각 호의 어느 하나에 해당하는 자에 대하여는 **2년 이내**의 범위에서 **입찰 참가자격을 제한**하여야 한다.

| 호 | 제한 사유 |
|----|---------|
| 1 | 계약을 이행할 때 **부실·조잡·부당한 이행**을 한 자 |
| 2 | **허위 서류**를 제출하여 계약을 체결하거나 이행한 자 |
| 3 | **입찰 담합** 또는 입찰의 공정성을 해치는 행위를 한 자 |
| 4 | **뇌물**을 제공하거나 요구한 자 |
| 5 | 계약 이행 중 **공사비를 편취**하거나 하자를 방치한 자 |
| 6 | 발주기관 승인 없이 **무단 하도급**을 한 자 |
| 7 | **안전·보건 조치 위반**으로 중대재해를 발생시킨 자 |
| 8 | 정당한 이유 없이 **계약 체결·이행을 거부**한 자 |
| 9 | 관계 서류를 **위조·변조**하거나 허위로 작성한 자 |
| 10 | 그 밖에 계약의 적정한 이행을 **현저히 해친 자** |

② 입찰 참가자격의 제한 기간·절차 등은 **대통령령으로 정한다.**

③ 지방자치단체의 장은 제재 처분 전 당사자에게 **의견을 진술할 기회**를 주어야 한다.

