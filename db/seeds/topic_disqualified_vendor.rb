## 예규 제324호 제9장 제10절 — 부정당업자 제재 실무

### 제재 이력 조회 방법

**1. 나라장터(g2b.go.kr) 조회**
1. 나라장터 접속 → [정보마당] → [부정당업자 현황]
2. 업체명 또는 사업자등록번호 입력
3. 현재 제재 여부 및 제재 기간 확인

**2. 조달청 시스템 조회**
- 조달청 홈페이지(pps.go.kr) → [입찰·계약 안내] → [부정당업자 조회]
- 국가기관 부과 제재도 통합 조회 가능

**3. 입찰 전 필수 확인**
- 입찰 공고 후 개찰 전 반드시 참가 업체의 부정당업자 이력 조회
- 제재 중인 업체가 낙찰될 경우 계약 무효 처리

---

### 제재 처분 전 체크리스트 (담당 공무원용)

| 항목 | 확인 내용 | 완료 |
|------|----------|------|
| **위반 사실 확인** | 증거 자료 확보 (계약서, 현장사진, 감사 결과 등) | ☐ |
| **사전 통지 발송** | 위반 사실·예정 기간·의견 제출 기한(7일↑) 서면 통지 | ☐ |
| **의견 진술 기회** | 당사자 의견서 수령 또는 청문 기회 부여 | ☐ |
| **감경·가중 사유 검토** | 초범 여부, 자진 신고, 반복 위반 여부 확인 | ☐ |
| **심의위원회 개최** | 제재 기간 결정 (기본·감경·가중 적용) | ☐ |
