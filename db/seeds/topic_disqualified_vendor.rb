| **나라장터 등록 요청** | 행정안전부 또는 조달청에 등록 요청 | ☐ |

---

### 청문 절차 진행 방법 (행정절차법 제22조)

**청문 신청 요건:**
- 부정당업자 제재는 침익적 행정행위로 당사자 신청 또는 처분청 직권으로 청문 가능

**청문 진행 절차:**
1. **청문 통지**: 청문일 10일 전까지 일시·장소·담당자 통지
2. **청문 실시**: 당사자 출석, 의견 진술, 증거 제출 기회 부여
3. **청문 주재자 보고**: 청문 결과를 처분청 장에게 보고
4. **처분 결정**: 청문 결과를 반영하여 최종 제재 기간 결정

**유의사항:**
- 청문 없이 제재 처분 시 절차적 하자로 행정소송 취소 사유가 될 수 있음
- 당사자가 청문 포기 서면 제출 시 생략 가능

---

### 타 기관 제재 시 적용 여부 (상호 인정)

| 제재 기관 | 적용 범위 |
|----------|---------|
| **국가기관** (조달청·중앙부처) 부과 제재 | 지방자치단체 입찰 자동 참가 불가 |
| **지방자치단체** 부과 제재 | 국가기관·타 지방자치단체 입찰 자동 참가 불가 |
| **공기업·준정부기관** 부과 제재 | 나라장터 등록 시 동일하게 적용 |

> 제재는 기관 단위가 아닌 **나라장터 통합 시스템**으로 관리되므로, 1개 기관에서 제재받으면 전국 모든 공공입찰에서 참가 불가합니다.

---

### 이의신청 방법

**행정심판 (행정심판법)**
- 처분 통보를 받은 날로부터 **90일 이내** 청구
- 관할 행정심판위원회(지방자치단체장 → 시·도 행정심판위원회)

**행정소송 (행정소송법)**
- 처분 통보를 받은 날로부터 **90일 이내** 제기
- 관할 행정법원

**집행정지 신청**
- 제재 처분에 대한 집행정지 신청 가능
- 회복하기 어려운 손해 발생 우려 소명 필요
