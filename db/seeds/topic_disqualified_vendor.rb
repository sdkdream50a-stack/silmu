## 지방계약법 제31조 (부정당업자의 입찰 참가자격 제한)

① 지방자치단체의 장 또는 계약담당자는 **다음 각 호의 어느 하나에 해당하는 자**에 대하여는 2년 이내의 범위에서 **입찰 참가자격을 제한**하여야 한다.

1. 계약을 이행할 때 **부실·조잡·부당한 이행**을 한 자
2. **허위 서류**를 제출하여 계약을 체결한 자
3. **입찰 담합** 행위를 한 자
4. **뇌물**을 제공하거나 요구한 자
5. 계약 이행 중 **공사비를 편취**한 자
6. 무단으로 **하도급**을 한 자
7. **안전·보건** 조치 위반으로 중대재해를 발생시킨 자
8. 정당한 이유 없이 **계약을 거부**한 자
9. **관계 서류를 위조·변조**한 자
10. 그 밖에 **계약의 적정한 이행**을 현저히 해친 자

② 제1항에 따른 입찰 참가자격의 제한 기간·절차 등에 관하여 필요한 사항은 **대통령령으로 정한다.**

③ 지방자치단체의 장은 제1항에 따른 입찰 참가자격을 제한하려면 미리 **당사자에게 의견을 진술할 기회**를 주어야 한다.

<div style="background:#eff6ff; padding:12px; border-radius:8px; margin-top:12px;">
<strong>법률 위임 구조</strong>

법률(제31조) → 시행령(제92조~제96조) → 예규 제324호 제9장 제10절

입찰 참가자격 제한의 구체적 사유별 기간, 감경·가중 기준, 절차는 시행령과 예규에서 규정합니다.
</div>

### 제재의 법적 성격

- **행정처분**: 침익적 행정행위로 행정쟁송(행정심판·행정소송) 대상
- **전국 적용**: 나라장터(g2b.go.kr) 등록으로 모든 공공기관에 일괄 적용
- **당연 참가 불가**: 제재 기간 중 입찰 참가, 수의계약 체결, 공동 수급 모두 불가
