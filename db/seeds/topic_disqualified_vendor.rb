### 제94조 — 가중 기준

다음 사유가 있을 경우 기본 제한 기간의 **2분의 1 범위** 내에서 가중할 수 있다. (최대 2년 초과 불가)

| 가중 사유 | 가중 범위 |
|----------|----------|
| **반복 위반**: 과거 3년 내 동종 위반 전력 | 최대 1/2 가중 |
| **고의성**: 계획적·조직적 위반 | 최대 1/2 가중 |
| **피해 규모 중대**: 공공사업 큰 피해 | 최대 1/3 가중 |
| **은폐·축소**: 위반 사실 은폐 시도 | 최대 1/3 가중 |

