- **입찰 참가 불가**: 제재 기간 중 모든 공공기관 입찰 참가 불가
- **수의계약 불가**: 수의계약 대상에서도 제외
- **공동 수급 불가**: 공동 수급체 구성원으로 참여 불가
- **하도급 불가**: 다른 계약의 하수급인으로도 참여 불가 (발주기관 승인 불가)
