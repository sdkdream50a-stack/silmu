### 제93조 — 감경 기준

다음 사유가 있을 경우 기본 제한 기간의 **2분의 1 범위** 내에서 감경할 수 있다.

| 감경 사유 | 감경 범위 |
|----------|----------|
| **자진 신고**: 위반 사실을 스스로 신고한 경우 | 최대 1/2 감경 |
| **경미한 위반**: 피해 규모가 작고 반복성 없는 경우 | 최대 1/2 감경 |
| **시정·보완**: 위반 후 즉시 자발적 시정 조치 | 최대 1/3 감경 |
| **초범**: 과거 5년간 제재 이력 없는 경우 | 최대 1/3 감경 |
| **경제적 어려움**: 중소기업·소상공인 등 | 참작 가능 |

