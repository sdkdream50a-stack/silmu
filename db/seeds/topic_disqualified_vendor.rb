### 제95조 — 제재 절차

**1단계: 사전 통지**
- 위반 사실, 제재 예정 기간, 의견 제출 기한(7일 이상) 서면 통지

**2단계: 의견 진술 기회 부여**
- 당사자는 서면 또는 구두로 의견 진술 가능
- 청문 신청 시 청문 절차 진행 (행정절차법 제22조)

**3단계: 제재 심의**
- 계약심의위원회 또는 내부 심의 (감경·가중 사유 검토)

**4단계: 제재 처분 결정 통보**
- 처분 사유, 제한 기간, 불복 방법(행정심판·행정소송) 명시

**5단계: 나라장터 등록**
- 행정안전부 → 조달청 나라장터에 등록 → 전국 공공기관 자동 적용

---

### 제96조 — 나라장터 등록 및 상호 인정

- **등록 즉시 전국 적용**: 등록 시점부터 해당 업체는 전국 모든 공공기관 입찰 참가 불가
- **상호 인정**: 국가계약법 상의 제재와 지방계약법 상의 제재 상호 인정
  - 국가기관(조달청, 중앙부처)에서 제재 → 지방자치단체 입찰도 자동 참가 불가
  - 지방자치단체에서 제재 → 국가기관 입찰도 자동 참가 불가
- **종료 후 자동 해제**: 제재 기간 만료일 다음 날 자동 해제, 별도 신청 불필요
