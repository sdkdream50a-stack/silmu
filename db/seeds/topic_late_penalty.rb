## 지체상금 관련 유권해석

### Q. 이행기한 연장 승인 시 지체상금이 면제되나?

**A:** **네.** 발주기관이 이행기한 연장을 **사전에 승인**한 경우, 연장된 기한까지는 지체상금이 부과되지 않습니다. 다만, 연장된 기한도 넘기면 그때부터 지체상금이 발생합니다.
- 근거: 시행령 제75조
- 연장 승인은 반드시 이행기한 **만료 전에** 결정해야 함

---

### Q. 부분 이행 시 지체상금은 어떻게 산정하나?

**A:** 기 이행 부분이 **독립적으로 사용 가능**한 경우, 기 이행 부분의 금액을 계약금액에서 공제하고 나머지 금액에 대해 지체상금을 산정합니다.
- 예: 10억원 계약 중 6억원분 이행 완료 → 4억원 기준으로 산정
- 기 이행 부분이 독립 사용 불가 시: 전체 계약금액 기준

---

### Q. 지체상금과 실손해를 동시에 청구할 수 있나?

**A:** **원칙적으로 불가합니다.** 지체상금은 손해배상의 예정이므로, 별도의 실손해 배상을 청구할 수 없습니다. 다만, 지체상금이 실제 손해보다 현저히 적은 경우 법원에 증액을 청구할 수 있다는 판례가 있습니다.

---

### Q. 검사 불합격으로 시정하는 기간도 지체일수에 포함되나?

**A:** **네, 포함됩니다.** 이행기한 후에 검사를 받아 불합격된 경우, 시정 후 재검사 합격일(또는 이행완료 통보일)까지의 기간이 지체일수에 산입됩니다. 이행기한 내 합격해야 지체상금이 발생하지 않습니다.
