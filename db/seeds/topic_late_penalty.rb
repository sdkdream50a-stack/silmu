## 지방계약법 시행령 — 지체상금 관련 조항

### 제74조 (지체상금률)

계약상대자의 귀책사유로 이행기한을 넘긴 경우, 지체일수에 지체상금률을 곱하여 산정한다.

| 계약 유형 | 지체상금률 (1일당) |
|----------|-----------------|
| **공사** | 계약금액의 **1/2,000** |
| **물품 제조·구매** | 계약금액의 **3/4,000** |
| **물품 수리·가공·대여, 용역** | 계약금액의 **3/4,000** |
| **임대차** | 계약금액의 **1/1,000** |

### 지체상금 산정 공식

> **지체상금 = 계약금액 × 지체상금률 × 지체일수**

**한도:** 지체상금의 합계액은 **계약금액을 초과할 수 없다.**

### 지체일수 산정

- **기산일:** 계약상 이행기한의 **다음 날**
- **종료일:** 이행 완료 통보일 (검사 합격일이 아님)
- **산입 제외:** 발주기관 귀책사유로 인한 지체일수는 제외

### 제75조 (지체상금 면제·감면)

다음 사유에 해당하는 기간은 지체일수에서 **제외**한다:

| 면제 사유 | 내용 |
|----------|------|
| **천재지변** | 불가항력적 사유 (태풍, 지진, 홍수 등) |
| **발주기관 귀책** | 설계변경 지연, 용지 미확보, 민원 발생 등 |
| **설계변경** | 발주기관 요청에 의한 설계변경 기간 |
| **관급자재 지급 지연** | 발주기관이 지급할 자재의 공급 지체 |
| **불가피한 사유** | 기타 계약상대자 귀책이 아닌 합리적 사유 |

### 부분 이행 시 지체상금 (제74조 제3항)

- 공사 등을 **부분적으로 이행**한 경우
- 기 이행 부분이 **독립적으로 사용 가능**하면
- 기 이행 부분의 금액을 계약금액에서 **공제**하고 지체상금 산정
