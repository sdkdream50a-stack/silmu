## 지체상금 질의답변

### Q1. 지체상금은 어떻게 계산하나요?

**A:** **계약금액 x 지체상금률 x 지체일수**로 계산합니다.
- 공사: 계약금액의 **1/2,000** (1일당)
- 물품·용역: 계약금액의 **3/4,000** (1일당)
- 임대차: 계약금액의 **1/1,000** (1일당)
- 한도: 계약금액을 초과할 수 없음

---

### Q2. 지체상금이 면제되는 경우는?

**A:** 다음 사유는 지체일수에서 제외됩니다.
- **천재지변:** 태풍, 지진, 홍수 등 불가항력
- **발주기관 귀책:** 설계변경 지연, 용지 미확보 등
- **관급자재 미지급:** 발주기관이 지급할 자재 공급 지체
- **기타 불가피 사유:** 계약상대자 귀책이 아닌 합리적 사유

---

### Q3. 지체상금은 대금에서 공제하나요?

**A:** **네.** 대금 지급 시 지체상금을 공제하고 나머지를 지급합니다.
- 대금 부족 시: 별도로 징수
- 계약보증금에서 공제도 가능
- 분쟁 시: 지체상금 금액을 확정한 후 공제

---

### Q4. 인력 부족으로 지연되면 면제받을 수 있나요?

**A:** **면제되지 않습니다.** 인력 확보는 계약상대자의 의무이므로 인력 부족은 계약상대자 귀책사유입니다. 천재지변, 발주기관 귀책 등 법에 열거된 사유만 면제 대상입니다.
