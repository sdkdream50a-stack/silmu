**예시 1: 공사 계약**
- 계약금액: 5억원
- 이행기한: 2025년 12월 31일
- 이행완료일: 2026년 1월 20일 (지체 20일)
- 지체상금: 5억 x 1/2,000 x 20일 = **500만원**

