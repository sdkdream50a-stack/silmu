### 물가변동 조정 (시행령 제73조) 절차

**요건 확인 → 방법 선택 → 등락률 산정 → 조정금액 계산 → 변경계약**

1. **조정 요건 확인**
   - 계약체결일(또는 직전 조정기준일)로부터 **90일 이상 경과**
   - 등락률 **3% 이상** 변동 (상승 또는 하락)

2. **조정 방법 선택** (계약상대자 선택, 이후 변경 불가)
   - **지수조정률:** 한국은행 물가지수 활용, 비목별 가중평균 — 산정 간편
   - **품목조정률:** 개별 품목 가격 직접 조사 — 정확하나 복잡

3. **선금 공제**
   - 조정금액 = 물가변동 조정금액 × (1 - 선금 비율)

4. **변경계약 체결** — 조정기준일로부터 30일 이내 신청

---

### 설계변경 조정 (시행령 제74조) 절차

**사유 발생 → 사전 승인 → 변경설계 작성 → 금액 산정 → 변경계약**

1. **설계변경 사유 확인** (시행령 제74조 각 호)
   - 설계서 오류·누락, 현장 조건 상이, 발주기관 요구 등

2. **사전 승인** — 승인 없이 시공 후 사후처리는 감사 지적

3. **변경 금액 산정**
   - 기존 비목: 계약단가 적용 (단, 계약단가 > 예정가격 단가인 경우 감소분은 예정가격 단가)
   - 신규비목: ① 변경 시점 단가 → ② 거래실례가격 → ③ 감정가격

4. **변경계약 체결** — 설계변경 완료일 또는 청구일로부터 **30일 이내**

---

### 기타변경 조정 (시행령 제75조) 절차

**변경 사유 발생 → 실비 산정 → 증빙 첨부 → 조정 신청 → 변경계약**

1. **기타변경 해당 여부 확인**
   - 물가변동·설계변경 어느 쪽에도 해당하지 않는 변경
   - 주요 사례: 법령 강화, 인허가 조건 변경, 발주기관의 일방적 지시

2. **실비 산정** — 추가 소요되는 실제 비용을 증빙으로 입증
   - 인건비, 자재비, 장비비 등 실제 발생 비용
   - 실비를 초과하는 조정은 불가

3. **증빙 자료 확보**
   - 법령·고시 변경 공문, 발주기관 지시 공문, 비용 발생 증빙

4. **변경계약 체결**

---

### 조정 제한 사항

| 제한 유형 | 내용 |
|----------|------|
| **계약 만료 후 조정** | 계약 기간이 종료된 후에는 조정 신청 불가 (공사 완료 후 물가변동 조정 신청 불인정) |
| **90일 미경과** | 물가변동은 직전 조정기준일로부터 90일 미경과 시 조정 불가 |
| **3% 미달** | 물가변동은 등락률 3% 미달 시 조정 불가 |
| **설계변경 10% 초과** | 총공사비 10% 초과 증가는 기관장 승인 등 추가 절차 필요 |
| **실비 초과** | 기타변경은 실비를 초과하여 조정 불가 |
