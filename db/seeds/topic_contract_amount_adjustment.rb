## 지방계약법 제22조 (물가변동 등에 의한 계약금액 조정)

① 지방자치단체의 장 또는 계약담당자는 공사·제조·용역 등의 계약을 체결한 후 **물가의 변동**, 설계변경, 그 밖에 계약내용의 변경으로 인하여 계약금액을 조정할 필요가 있을 때에는 **대통령령으로 정하는 바에 따라** 계약금액을 조정한다.

② 제1항에 따른 계약금액의 조정 요건, 조정 방법, 조정 기준 등에 관한 사항은 **대통령령으로 정한다.**

### 3가지 조정 유형의 법적 위치

| 유형 | 시행령 근거 | 핵심 요건 |
|------|----------|----------|
| **물가변동** | **제73조** | 90일 경과 + 등락률 3% 이상 |
| **설계변경** | **제74조** (구 제65조·제66조) | 물량·공법 변경 |
| **기타변경** | **제75조** | 법령 변경, 발주기관 지시 등 |

> 세 가지 유형은 모두 지방계약법 제22조에 근거하며, 시행령에서 각각의 조정 방법을 구체적으로 규정한다

### 지방계약법 시행령 제73조 (물가변동으로 인한 계약금액의 조정)

계약 체결 후 **90일 이상 경과**하고 등락률이 **3% 이상** 변동한 경우 계약금액을 조정한다.

- 조정 방법: 지수조정률 또는 품목조정률 (계약상대자 선택)
- 선금 지급분은 조정 대상에서 제외
- 물가 하락 시에도 감액 조정 의무

### 지방계약법 시행령 제74조 (설계변경으로 인한 계약금액의 조정)

공사계약에서 설계서의 변경으로 공사량의 증감이 발생한 경우 계약금액을 조정한다.

- 조정 기준: 증감 물량 x 계약단가 (신규비목은 변경 시점 단가)
- 설계변경 완료일 또는 청구일로부터 **30일 이내** 조정
- 총공사비의 10%를 초과하는 증가는 기관장 승인 필요

### 지방계약법 시행령 제75조 (기타 계약내용의 변경으로 인한 계약금액의 조정)

물가변동·설계변경 외 **법령 변경, 발주기관의 지시** 등 계약 내용 변경으로 계약금액 조정이 필요한 경우 **실비를 초과하지 않는 범위에서** 조정한다.

- 조정 기준: 실비 정산 방식 (추가 소요 비용의 실제 금액)
- 적용 사례: 안전법령 강화, 환경 기준 변경, 인허가 조건 변경 등
