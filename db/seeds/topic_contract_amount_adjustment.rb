## 실무 적용 기준

### 조정 유형 판단 기준

현장에서 계약금액 조정 사유가 발생했을 때 적용 유형을 판단하는 순서:

1. **설계서·물량이 변경되었는가?**
   - Yes → **설계변경 조정** (시행령 제74조)
   - No → 다음 단계로

2. **물가지수가 변동하였는가? (90일 경과, 3% 이상)**
   - Yes → **물가변동 조정** (시행령 제73조)
   - No → 다음 단계로

3. **법령 변경·발주기관 지시 등 외부 원인인가?**
   - Yes → **기타변경 조정** (시행령 제75조)
   - No → 조정 불가

### 혼합 발생 시 처리

물가변동과 설계변경이 **동시에 발생**한 경우:
- **각 유형을 별도로 산정·신청** — 하나의 변경계약에서 동시 처리 가능
- 이중 반영(같은 비용을 두 가지 유형으로 중복 청구) 금지
- 설계변경으로 증감된 금액은 **설계변경 시점 단가** 기준
- 물가변동 조정은 **원래 계약금액** 기준 (설계변경분 제외 후 산정하는 것이 원칙)

### 조정 신청 시 유의사항

**계약상대자 입장:**
- 조정 요건 충족 즉시 신청 (지체 시 불이익 없으나, 계약 만료 후에는 신청 불가)
- 설계변경은 **사전 승인** 후 시공 (무단 변경 시공 시 조정 불인정 가능)
- 물가변동 조정 방법(지수/품목) 최초 선택 후 변경 불가

**발주기관 입장:**
- 조정 신청 접수 후 **30일 이내** 처리 의무
- 물가 하락 시 **직권으로 감액 조정** 가능 (계약상대자 동의 불요)
- 기타변경은 실비 증빙을 철저히 검토하여 과다 조정 방지

### 하도급 연동 조정

원도급 계약금액이 물가변동으로 조정되면:
- 원수급자는 **하도급 대금도 조정 비율에 따라 연동 조정** 의무 (하도급법 제16조의2)
- 설계변경의 경우에도 실제 변경 내용이 하도급 범위에 해당하면 하도급 대금 조정 필요
