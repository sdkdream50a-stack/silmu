  ## 지방자치단체 입찰 및 계약집행기준 제7장 (종합계약 실무)

  ### 장기계속계약 연차 계약서 작성법

  **1차년도 계약서 필수 기재 사항:**

  | 항목 | 내용 |
  |------|------|
  | 1차년도 계약금액 | 해당 연도 예산 범위 내 금액 |
  | 총 계약금액 (부기) | 입찰 결과 확정된 전체 금액 |
  | 1차년도 이행기간 | 해당 연도 완료 기한 |
  | 총 이행기간 (부기) | 전체 공사 완료 예정일 |

  > ※ 총액·총 이행기간을 **부기하지 않으면** 차년도 계약의 법적 근거가 소멸합니다.

  ### 연차 계약 체결 절차

  1. **예산 확정 확인** — 해당 연도 세출예산에 사업비 반영 여부 확인
  2. **전년도 이행 점검** — 전년도 이행 완료 여부 및 잔여 물량 확인
  3. **계약금액 산정** — 연차분 예정가격 작성 (물가변동 반영 가능)
  4. **계약서 작성** — 연차 계약금액·이행기간 기재
  5. **계약 체결** — 계약상대자 날인 및 보증서 갱신

  ### 단가계약 운영 실무

  - **상한금액 설정 필수**: 계약 체결 시 예정 수량 × 단가로 상한금액을 설정하고, 이를 초과하는 발주 불가
  - **발주서 발행**: 매 발주 시 품목·수량·납품 기한·납품 장소를 명시한 발주서 발행
  - **상한금액 초과 시**: 추가 계약 체결 또는 재입찰 필요 (임의 초과 발주 위법)
  - **계약 기간 갱신**: 1년 경과 시 재입찰·재계약 원칙

  ### 계약 유형 선택 기준

  | 상황 | 권장 계약 유형 |
  |------|-------------|
  | 총 예산 확보, 단기 이행 | 총액계약 |
  | 다년도 이행, 연도별 예산 | 장기계속계약 |
  | 지방의회 총액 의결 완료 | 계속비계약 |
  | 수량 미확정, 반복 발주 | 단가계약 |
CONTENT

