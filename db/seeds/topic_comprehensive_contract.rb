  ## 지방계약법 제21조·제22조 (장기계속계약·계속비계약·단가계약)

  **지방계약법 제21조 (장기계속계약 및 계속비계약)**

  <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
  ① 각 연도 예산 범위에서 계약을 체결하는 경우로서 이행 기간이 수 년도에 걸치는 경우에는 대통령령으로 정하는 바에 따라 **장기계속계약**을 체결할 수 있다.

  ② 지방의회의 의결을 거친 경비로 이행에 수 년도가 걸리는 경우에는 대통령령으로 정하는 바에 따라 **계속비계약**을 체결할 수 있다.
  </div>

  **지방계약법 제22조 (단가계약)**

  <div style="background:#dbeafe;padding:12px;border-radius:8px;margin:8px 0;">
  ① 일정 기간 계속하여 제조·수리·가공·매매·공급·사용 등의 계약을 할 필요가 있을 때에는 **단가에 의한 계약**을 체결할 수 있다.

  ② 단가계약을 체결하는 경우에는 계약 단가의 적정성, 예정 수량 등을 고려하여 **계약금액의 상한**을 정하여야 한다.
  </div>

  ### 총액계약 vs 단가계약 비교

  | 구분 | 총액계약 | 단가계약 |
  |------|----------|----------|
  | 수량 확정 시점 | 계약 체결 시 확정 | 계약 체결 시 미확정 |
  | 금액 산정 방식 | 전체 계약금액 일괄 | 단가 × 발주 수량 |
  | 주요 적용 대상 | 단일 공사·프로젝트 | 소모품, 반복 서비스, 유지보수 |
  | 이행 방식 | 계약 내용 일괄 이행 | 발주서별 개별 이행 |

  ### 장기계속계약 체결 요건

  - 이행 기간이 **수 년도에 걸치는** 공사·용역 계약
  - 총 예산을 일괄 확보하지 않고 **연도별 예산 범위** 내에서 분할 체결
  - 지방의회 의결 불필요 (연도별 세출예산 범위 내)
  - 1차년도 계약서에 **총 계약금액·총 이행기간 부기(附記)** 필수

  <div style="background:#f3f4f6;padding:12px;border-radius:8px;margin:8px 0;">
  ※ 관련 법령: 지방계약법 제21조·제22조, 지방계약법 시행령 제72조~제76조, 지방자치단체 입찰 및 계약집행기준(예규 제324호) 제7장
  </div>
