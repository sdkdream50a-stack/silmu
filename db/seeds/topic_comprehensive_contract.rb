  ## 지방계약법 시행령 제72조~제76조 (종합계약 운영)

  ### 제72조 (장기계속계약)

  <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
  ① 장기계속계약을 체결할 때에는 계약서에 다음 사항을 명시하여야 한다.
  1. **총 계약금액** (입찰 결과로 확정된 금액)
  2. **총 이행기간**
  3. 각 **연도별 계약금액 및 이행기간**

  ② 각 연도별 계약은 해당 연도 **예산이 확정된 후** 체결한다.

  ③ 차년도 계약 체결은 전년도 계약 이행 상황 확인 후 진행한다.
  </div>

  ### 장기계속계약 연차별 계약 방법

  | 연차 | 계약 내용 | 주의사항 |
  |------|-----------|---------|
  | 1차년도 | 총액 입찰·낙찰 → 1차년도분 계약 체결 | **총액·총 이행기간 부기 필수** |
  | 2차년도 | 해당 연도 예산 확정 후 2차년도분 계약 | 예산 미확정 시 계약 불가 |
  | 3차년도 이후 | 동일 절차 반복 | 이행 상황 확인 후 체결 |

  ### 제73조 (계속비계약)

  <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
  계속비계약은 **지방의회의 의결을 거친 계속비 예산**이 확보된 경우에 총 공사금액과 총 이행기간을 **1개의 계약**으로 체결한다.
  </div>

  ### 종합계약과 계속비 예산의 차이

  | 구분 | 장기계속계약 | 계속비계약 |
  |------|------------|-----------|
  | 예산 확보 방식 | 연도별 세출예산 | 지방의회 의결 계속비 예산 |
  | 총액 확보 여부 | 불필요 (연도별 분할) | 필요 (총액 일괄 의결) |
  | 계약 횟수 | 연도별 분할 체결 | 1회 전체 체결 |
  | 적용 사례 | 일반 다년도 공사·용역 | 대규모 확정 인프라 공사 |
  | 중도 중단 위험 | 예산 미확보 시 가능 | 낮음 (총액 확보 상태) |

  ### 제74조 (단가계약 수량 확정 절차)

  <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
  ① 단가계약은 다음 각 호의 경우에 체결할 수 있다.
  1. 품목의 성질상 미리 수량을 확정하기 곤란한 경우
  2. 같은 품목을 여러 수요기관이 이용할 경우
  3. 계속적으로 소량씩 발주할 필요가 있는 경우

  ② 수요기관은 발주 시 **발주서**를 발행하여야 하며, 품목·수량·납품 장소 및 기한을 명시한다.

  ③ 단가계약 기간은 **1년 이내**를 원칙으로 하되, 부득이한 경우 2년 이내로 할 수 있다.
  </div>

  ### 제75조 (장기계속계약 중도 해지 규정)

  <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
  ① 장기계속계약에서 **예산 미확보** 등 부득이한 사유로 차년도 계약을 체결할 수 없게 된 경우, 계약담당자는 계약상대자에게 **사전 통보**하여야 한다.

  ② 계약 해지 시 기 이행된 연차분에 대한 대가는 정산·지급하여야 한다.

  ③ 정당한 사유 없는 해지로 손해가 발생한 경우 계약상대자는 손해배상을 청구할 수 있다.
  </div>

  ### 제76조 (설계변경 및 물가변동 관리)

  <div style="background:#dcfce7;padding:12px;border-radius:8px;margin:8px 0;">
  - 설계변경은 **총 계약금액 기준**으로 관리하며, 잔여 연차에 반영
  - 물가변동(ESC) 적용도 **총액 기준**으로 산정
  - 이미 이행 완료된 연차분은 소급 변경하지 않음
  </div>
