담당자는 이 사업을 "기성 소프트웨어 패키지 구매"로 분류하여, 지방계약법 시행령 제25조 제1항 제1호의 물품 수의계약 기준(2,000만원 초과 시 경쟁 원칙)을 무시하고 특정 IT 업체 A사와 직접 수의계약을 체결했습니다.

### 계약 유형 오분류 경위

실제 계약 내용을 살펴보면 다음과 같았습니다.

| 항목 | 담당자 분류(물품구매) | 실제 성격(용역계약) |
