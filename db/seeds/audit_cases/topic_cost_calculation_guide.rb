### 2. 이전 담당자 파일을 그대로 쓰면 안 됩니다
원가계산서를 전임자 파일에서 복사해 사용하는 관행은 매우 위험합니다. 예규는 매년 개정되며, 보험요율도 매년 변동됩니다. **반드시 최신 예규를 직접 확인하고 비율을 입력**해야 합니다.

