"설마 들킬까?"는 착각입니다. 감사원·시도 감사는 수의계약을 집중적으로 점검합니다.

### 3. 이해충돌 방지 의무를 준수하세요
친척, 지인과 이해관계가 있는 계약은:
- **사전에 기관에 신고** (이해충돌 방지법)
- **해당 계약 업무에서 배제** (회피 의무)
- **미신고 시 과태료** + **계약 무효** 가능

"친척을 돕는다"는 마음이 본인과 친척 모두를 파멸로 이끕니다.

### 4. 경쟁입찰 가능 여부를 정확히 판단하세요
수의계약 검토 시:
- 다른 업체도 수행 가능한지 확인
- 특수 장비·기술이 정말 필요한지 검증
- 경쟁입찰 시간적 여유가 정말 없는지 판단

**"이 업체만 할 수 있다"는 주장은 대부분 허위**입니다. 최소 2~3개 업체는 수행 가능합니다.

### 5. 수의계약 비율을 관리하세요
지자체 전체 계약 중 수의계약 비율이 높으면:
- 감사원 특별감사 대상
- 언론 보도로 행정 신뢰도 하락
- 전체 계약 절차 재검토

**적정 수의계약 비율**: 전체 계약의 30% 이하 (권장)

수의계약이 50% 이상이면 "감사 대상 기관"으로 분류됩니다.

