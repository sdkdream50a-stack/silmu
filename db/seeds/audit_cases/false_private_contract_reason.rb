**"편의상", "관례"는 수의계약 사유가 아닙니다.**

### 2. 수의계약 사유는 '객관적 사실'이어야 합니다
사유서에 작성하는 내용은:
- **객관적으로 입증 가능**해야 함 (견적서, 시장조사, 특허증 등)
