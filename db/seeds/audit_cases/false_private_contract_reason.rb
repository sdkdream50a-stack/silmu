- 구청장 정치적 책임 (사과 발표)

### 왜 이런 일이?

**I씨의 진술**: "친척이 운영하는 회사를 돕고 싶었다. 다른 업체도 할 수 있다는 것은 알았지만, 설마 감사에 걸릴 줄은 몰랐다."

→ **사익 추구 + 법령 경시**가 결합된 결과입니다.

### 수의계약 사유의 엄격성

수의계약은 **예외적**인 계약 방법입니다. 원칙은 **경쟁입찰**이며, 수의계약은 **법령에 명시된 사유에만** 가능합니다.

**수의계약 허용 사유** (지방계약법 시행령 제25조):
1. 금액 기준 (물품 2천만원 이하 등)
2. 천재지변 등 긴급
3. 특허·실용신안권자와 계약
4. 경쟁에 부칠 여유가 없는 경우
5. 기타 법령에 명시된 경우

**"편의상", "관례적으로"는 수의계약 사유가 아닙니다.**

