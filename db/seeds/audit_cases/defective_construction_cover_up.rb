## 실무자가 반드시 기억해야 할 5가지

### 1. 감독은 '현장'에서 이루어집니다
사무실에서 서류만 보는 것은 감독이 아닙니다. 반드시:
- **주기적 현장 방문** (주 1회 이상)
- **중요 시공 단계 입회** (콘크리트 타설, 철근 배근, 방수층 시공)
