### 4. 부실 시공은 결국 드러납니다
"설마 들킬까?"라는 생각은 착각입니다:
- 균열, 누수, 침하 등 시간이 지나면 드러남
- 감사원 특별감사, 내부고발, 언론 제보 등 적발 경로 다양
- 적발 시 전면 재시공 + 손해배상 + 형사 처벌

**부실 시공을 눈감아주는 것은 공범**입니다.

### 5. 국민 안전이 최우선입니다
교량, 건물, 도로 등 공공시설은 **국민의 안전과 직결**됩니다:
- 부실 시공 → 붕괴, 사고, 인명 피해 가능성
- 감독과 검사는 국민 안전의 최후 보루
- "이 정도면 괜찮겠지"는 절대 금물

