"귀찮다"는 이유로 생략하면 D씨처럼 파면과 실형을 받을 수 있습니다.

### 2. 준공검사는 '실물'을 확인하는 것입니다
서류와 사진만 보고 통과시키지 마세요:
- **시방서 기준 대조** (두께, 간격, 강도 등)
- **코어 채취 및 시험** (콘크리트 강도)
- **숨겨진 부분 확인** (철근 배근, 방수층)

"형식적 검사"는 E씨처럼 파면과 형사 처벌로 이어집니다.

### 3. 금품 제공은 '뇌물'입니다
업체가 제공하는 금품(식사, 선물, 현금)은 **모두 뇌물**입니다:
- "한 끼 식사" → 수뢰죄
- "명절 선물" → 수뢰죄
- "사례금" → 수뢰죄

