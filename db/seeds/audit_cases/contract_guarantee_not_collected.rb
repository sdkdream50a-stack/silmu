### 계약보증금의 의미

계약보증금은 **계약 이행을 담보하는 보증 수단**입니다. 계약자가 채무불이행 시:
1. 보증금을 국고 귀속
2. 해당 금액으로 나머지 공사 완수 또는 손실 보전

**계약보증금 없이 계약을 체결하는 것은 재정 리스크를 방치하는 것**과 같습니다.

### 면제 사유를 오해한 사례

C씨는 "업체 신용도가 높으면 면제할 수 있다"고 잘못 알고 있었습니다. 하지만 계약보증금 면제는 **매우 제한적인 경우**에만 가능합니다:

**계약보증금 면제 가능 경우** (지방계약법 시행규칙 제49조):
1. 국가·지방자치단체·공공기관과의 계약
2. 법률로 설립된 협회·조합과의 계약
3. 천재지변 등 긴급 계약
4. 계약금액 3천만원 미만 물품·용역

**일반 민간 건설업체는 신용도가 높아도 면제 불가**합니다.

