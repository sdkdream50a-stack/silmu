## 실무자가 반드시 기억해야 할 5가지

### 1. 계약보증금은 '필수'입니다
계약보증금 면제는 **매우 제한적인 경우**(국가기관, 법정협회, 긴급계약, 소액계약)에만 가능합니다. 일반 민간 업체는 신용도와 무관하게 **반드시 징구**해야 합니다.

### 2. 계약보증금 = 재정 손실 방지 수단
계약보증금이 없으면:
- 업체 부도 시 회수 수단 없음
- 공사 중단 시 추가 예산 부담
- 하도급 미지급 책임까지 전가

**계약보증금은 재정을 지키는 최후의 방어선**입니다.

### 3. 계약서 작성 전 보증금 조항 확인
계약서에 계약보증금 조항이 없으면 **계약 자체가 무효**입니다. 계약서 작성 시:
- 보증금 금액: 계약금액의 10% (공사), 5% (물품·용역)
- 보증 방법: 현금, 보증보험증권, 보증서
- 제출 기한: 계약 체결일로부터 10일 이내

### 4. 보증보험증권 유효성 확인
업체가 제출한 보증보험증권을 반드시 확인하세요:
- 보증기관 진위 확인 (서울보증보험, 농협, 새마을금고 등)
- 보증금액이 계약금액과 일치하는지
- 유효기간이 계약기간을 커버하는지

**위조 증권 사례**도 간혹 발생하므로, 보증기관에 직접 확인하는 것이 안전합니다.

### 5. 계약보증금 미징구는 징계 사유
계약보증금을 징구하지 않은 것은 **법령 위반**이며, 재정 손실 발생 시 담당자가 **징계 및 손해배상 책임**을 질 수 있습니다.

