## 사건 개요

2023년 6월, □□군은 주민 체육 활동 지원을 위한 **체육시설 조성공사(계약금액 8억원)**를 ◇◇건설과 일반경쟁입찰로 계약 체결했습니다.

계약 체결 당시, 계약담당 공무원 C씨는 **계약보증금 징구를 누락**했습니다. (정상: 계약금액의 10% = 8천만원)

### 공사 중단 사태

공사가 60% 정도 진행된 2024년 2월, ◇◇건설이 **부도로 법정관리에 들어가면서 공사가 전면 중단**되었습니다.

계약보증금이 없었기 때문에:
