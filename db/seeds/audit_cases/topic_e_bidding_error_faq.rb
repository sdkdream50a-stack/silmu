### 나라장터 오류 실제 원인 (사후 확인)

감사 과정에서 나라장터 고객센터에 사후 확인한 결과, 해당 오류는 **특정 브라우저(구형 Internet Explorer) 환경에서만 발생하는 클라이언트 측 문제**였으며, Chrome 등 표준 브라우저에서는 정상 작동하고 있었습니다.

