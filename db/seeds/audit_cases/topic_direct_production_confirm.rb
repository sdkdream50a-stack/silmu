### 1. 중소기업확인서 ≠ 직접생산확인증명서
두 서류는 완전히 다릅니다. 중소기업 우선구매 수의계약에서는 **반드시 직접생산확인증명서를 별도로 징구**해야 합니다. 중소기업확인서만으로는 요건이 충족되지 않습니다.

