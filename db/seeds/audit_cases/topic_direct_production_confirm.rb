### 실제 납품 경위

감사 과정에서 ㈜○○오피스는 복합기를 직접 제조하지 않고 **일본 ◆◆사 제품을 수입·유통하는 판매 대리점**으로 확인되었습니다.

- 직접 생산 여부: ❌ (제조 공장 없음)
- 직접생산확인증명서: 미보유 (취득 불가 품목)
- 실질: 수입 유통업체와의 계약

### 중소기업 우선구매 제도 취지 몰각

중소기업제품 구매촉진법의 취지는 **국내 중소 제조업체의 판로를 지원**하는 것입니다. 직접 제조하지 않는 유통·판매 업체가 혜택을 가로채면 제도 취지가 완전히 몰각됩니다.

| 원래 제도 취지 | 실제 결과 |
|--------------|-----------|
| 국내 중소 제조업체 판로 지원 | 일본산 제품 수입 유통업체 혜택 |
| 국산 제품 사용 촉진 | 수입 복합기 공공기관 납품 |
| 중소기업 생산 역량 강화 | 제도 우회 수단으로 악용 |

