### 2. 선금 한도를 준수하세요
선금 지급 한도 (지방계약법 시행규칙 제72조):
- **공사**: 계약금액의 70% 이하
- **물품 제조**: 계약금액의 70% 이하
- **물품 구매**: 계약금액의 30% 이하

한도를 초과하여 지급하면 담당자가 손해배상 책임을 질 수 있습니다.

### 3. 선금 사용처를 추적하세요
선금 지급 후:
- 자재 구매 영수증 제출 요구
- 현장 방문하여 자재 입고 확인
- 공사 진척률과 선금 사용액 대조

**선금을 지급하고 방치하면 유용될 가능성 90%**입니다.

### 4. 선금 보증서 유효성을 확인하세요
업체가 제출한 선금 보증서를 반드시 확인:
- 보증기관 진위 확인 (서울보증보험, 농협, 새마을금고 등에 전화)
- 보증금액이 선금액과 일치하는지
- 유효기간이 계약기간을 커버하는지

**위조 보증서 사례**도 있으므로 보증기관 직접 확인 필수.

### 5. "급하다"는 말에 속지 마세요
업체가 "자재 납기가 급하다", "공사 일정이 촉박하다"며 빠른 선금 지급을 요구해도:
- 법령상 지급 조건을 **반드시** 확인
- 조건 미충족 시 **절대 지급 불가**

"급하다"는 이유로 절차를 생략하면 G씨처럼 파면과 손해배상 책임을 지게 됩니다.

