이처럼 **지체상금이 결국 발주기관에 돌아오는 돈임을 인식하지 못한 채** 업체와의 관계를 고려해 임의로 연장해주는 관행이 빈번히 발생합니다. 이는 **재정 손실**이자 **법령 위반**입니다.
