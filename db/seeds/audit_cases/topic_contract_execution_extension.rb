## 실무자가 반드시 기억해야 할 사항

### 1. 계약기간 연장 = 법령상 열거된 사유에 한정
지방계약법 시행령 제72조는 공사기간 연장 사유를 **열거적으로 규정**하고 있습니다. 업체의 단순 공정 지연, 자체 인력·장비 부족은 연장 사유가 **절대 아닙니다**.

연장 가능 사유 요약:
- 천재지변·불가항력
- 발주기관 귀책 사유 (설계변경, 현장 조건 변경, 발주자 지시)
- 시장 전체의 원자재 공급 차질 (업체 귀책 아닌 경우)

### 2. 지체상금은 지자체의 재산
지체상금은 업체를 '벌'주는 것이 아니라 **지자체 재정을 보호하는 수단**입니다. 임의 연장으로 지체상금을 면제해주면 그 금액만큼 지자체 예산이 손실됩니다.

### 3. 연장 시 반드시 위원회 심의
계약 내용 변경(기간 포함)은 원칙적으로 **관련 위원회(계약심의위원회 등) 심의**를 거쳐야 합니다. 담당자 단독 판단으로 연장 처리하는 것 자체가 절차 위반입니다.

### 4. 업체의 요청을 그대로 수용하면 안 됩니다
업체가 연장을 요청할 때는 반드시 다음을 확인하세요:
- 연장 사유가 시행령 제72조 각호에 해당하는지
- 연장 사유를 입증하는 객관적 자료(기상자료, 공문, 설계변경서 등)가 있는지
- 위원회 심의 절차가 필요한지

### 5. 지체상금 부과 회피를 묵인하면 징계 대상
정당한 사유 없이 기간을 연장해 지체상금을 면탈시키면 담당자가 **징계(감봉, 견책 이상)**를 받을 수 있으며, 손해배상 청구 대상이 될 수도 있습니다.
