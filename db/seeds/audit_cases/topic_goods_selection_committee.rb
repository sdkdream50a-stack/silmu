### 물품선정위원회가 존재하는 이유

물품선정위원회는 단순한 행정 절차가 아닙니다. 이 위원회를 통해:
- **담당자 개인의 자의적 판단 방지**
- **업체 선정의 객관성·투명성 확보**
- **가격·품질·납기 기준으로 복수 업체 비교**
- **특정 업체 특혜 의혹 사전 차단**

위원회를 거치지 않는 것 자체가 담당자에게 가장 큰 리스크입니다.
