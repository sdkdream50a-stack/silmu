## 지방계약법 시행령 — 하도급 관련 조항

### 제56조 (하도급의 통지)

① 계약상대자가 하도급을 하려는 경우 **계약 체결 전까지** 다음 사항을 지방자치단체의 장에게 **통지**하여야 한다:

| 통지 사항 | 내용 |
|----------|------|
| **하수급인** | 상호·대표자·주소·사업자등록번호 |
| **하도급 내용** | 하도급 공종·범위·금액 |
| **하도급 비율** | 도급금액 대비 하도급금액 비율 |
| **하도급 사유** | 하도급이 필요한 사유 |
| **시공능력** | 하수급인의 등록·면허·실적 등 |

② 통지 시 **하도급 계약서 사본**을 첨부하여야 한다.

③ 하도급 내용을 **변경**하는 경우에도 변경 사항을 통지하여야 한다.

### 제57조 (하도급의 제한)

① 계약상대자는 **계약의 전부를 하도급**할 수 없다 (일괄하도급 금지).

② 계약상대자는 하도급받은 부분의 **전부를 다시 하도급(재하도급)** 할 수 없다.

③ 하도급의 비율에 관한 제한:

| 구분 | 하도급 제한 비율 |
|------|---------------|
| **일반 원칙** | 도급금액의 **50% 이상**을 직접 시공 |
| **전문공사** | 해당 전문공사 도급금액의 **50% 이상** 직접 시공 |
| **공동도급** | 각 구성원의 분담 부분에 대해 적용 |

④ 다음의 경우에는 하도급을 **제한·금지**할 수 있다:
- 계약의 성질상 직접 이행이 필요한 경우
- 하수급인의 시공능력이 부족한 경우
- 불공정한 하도급으로 인정되는 경우

### 제58조 (하도급대금의 지급 등)

① 하수급인에 대한 하도급대금의 **직접지급** 요건:

| 직접지급 사유 | 내용 |
|-------------|------|
| **원도급자 부도·파산** | 원도급자가 도급대금을 지급할 수 없게 된 경우 |
| **하수급인 직접지급 요청** | 하수급인이 직접지급을 요청하고 원도급자가 동의한 경우 |
| **원도급자 하도급대금 미지급** | 2회 이상 하도급대금을 지급하지 않은 경우 |
| **하도급법에 따른 직접지급** | 하도급거래 공정화에 관한 법률에 따른 사유 |

② 직접지급 시 **기성부분에 상당하는 금액**을 하수급인에게 직접 지급

③ 직접지급한 금액은 **원도급자에 대한 대금 지급으로 간주**

### 하도급 관련 벌칙·제재

- 일괄하도급 적발 시: **부정당업자 제재** (입찰참가자격 제한)
- 하도급 통지 의무 위반: 시정 요구, 반복 시 제재
- 불공정 하도급: 하도급법에 따른 과징금 부과 가능
