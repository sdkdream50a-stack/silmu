## 지방계약법 제18조 (하도급의 승인 등)

① 지방자치단체의 장 또는 계약담당자는 **필요하다고 인정**하면 계약상대자에 대하여 계약의 **전부 또는 주요 부분의 대부분을 하도급**할 수 없도록 할 수 있다.

② 계약상대자는 계약의 이행에 있어서 하도급을 하려면 **대통령령으로 정하는 바에 따라** 미리 지방자치단체의 장 또는 계약담당자에게 **통지**하여야 한다.

③ 지방자치단체의 장 또는 계약담당자는 하수급인(하도급을 받은 자)의 시공능력 등이 부족하여 **계약이행이 곤란하다고 인정**되면 하도급의 변경을 요구할 수 있다.

④ 하도급에 관한 사항은 **대통령령으로 정한다.**

### 하도급의 법률적 위치

- **목적:** 계약 이행의 품질 확보와 불공정 하도급 방지
- **성격:** 계약상대자의 이행 책임을 전제로 부분적 하도급 허용
- **핵심 원칙:** 일괄하도급(전부 하도급) 금지, 통지 의무
- **위임:** 하도급 통지, 제한, 대금 지급 등의 세부 사항은 시행령에 위임

> 하도급은 전문 분야의 효율적 시공을 위해 허용되나, 일괄하도급은 원칙적으로 금지

### 관련 조항 (지방계약법)

- **제15조 (계약보증금):** 원도급자가 이행 불능 시 보증금 귀속
- **제16조 (검사):** 하도급 부분을 포함한 전체 이행에 대해 검사
- **제17조 (대가의 지급):** 하도급대금 직접지급 요건 발생 시 하수급인에게 직접 지급 가능
- **제31조 (부정당업자 입찰참가자격 제한):** 부당 하도급 시 제재 가능

### 관련 법률 (건설산업기본법)

- **제29조 (건설공사의 하도급 제한):** 도급받은 건설공사의 전부 또는 주요 부분 대부분을 다른 건설사업자에게 하도급 금지 (일괄하도급 금지)
- **제29조의2 (건설공사의 하도급 제한에 관한 특례):** 하수급인은 하도급받은 건설공사를 다시 하도급 불가 (원칙). 예외: 업종이 다른 전문건설사업자에 대한 부분 재하도급
- **제29조의3 (건설공사의 직접 시공):** 건설사업자는 대통령령으로 정하는 비율 이상을 직접 시공

