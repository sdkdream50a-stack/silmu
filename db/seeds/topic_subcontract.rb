## 하도급 관련 유권해석

### Q. 전문공사 부분을 전문업체에 하도급하는 것도 50% 제한에 포함되나?

**A:** **네, 포함됩니다.** 전문공사를 전문건설사업자에게 하도급하더라도 원도급자는 도급금액의 **50% 이상을 직접 시공**해야 합니다. 다만, 전문공사 면허가 필요한 공종은 해당 면허를 보유한 업체에 하도급하는 것이 일반적이며, 이 경우에도 전체 하도급 비율이 50%를 넘지 않아야 합니다.
- 근거: 시행령 제57조, 건설산업기본법 제29조

---

### Q. 하도급 통지를 하지 않고 하도급한 경우 계약을 해지할 수 있나?

**A:** 하도급 통지 의무 위반 자체만으로 바로 계약 해지 사유가 되지는 않습니다. 다만, 발주기관은 **시정을 요구**할 수 있으며, 시정 불이행 시 또는 반복 위반 시 **계약 해지** 및 **부정당업자 제재** 사유가 될 수 있습니다. 특히 무통지 하도급이 일괄하도급에 해당하면 즉시 제재 대상입니다.
- 근거: 시행령 제56조, 제57조

---

### Q. 하수급인이 원도급자 동의 없이 발주기관에 하도급대금 직접지급을 요청할 수 있나?

**A:** 원도급자의 **동의가 있는 경우**에만 직접지급이 가능합니다. 다만, 원도급자가 **부도·파산** 등으로 대금을 지급할 수 없는 경우이거나, **2회 이상 하도급대금을 미지급**한 경우에는 원도급자의 동의 없이도 직접지급 사유가 발생합니다.
- 근거: 시행령 제58조, 하도급법 제14조

---

### Q. 공동도급의 구성원이 자신의 분담 부분을 하도급할 수 있나?

**A:** **가능합니다.** 공동도급 구성원은 자신의 분담 부분에 대해 하도급할 수 있습니다. 다만, 분담 부분의 **50% 이상을 직접 시공**해야 하며, 분담 부분 전부를 하도급(일괄하도급)할 수는 없습니다. 공동도급 구성원 간의 분담 비율 변경과 하도급은 구별됩니다.
- 근거: 시행령 제57조, 공동계약운용요령
