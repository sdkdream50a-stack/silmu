## 설계변경 관련 유권해석

### Q. 계약상대자가 설계변경을 요청할 수 있는가?

**A:** **네, 가능합니다.** 계약상대자는 공사 현장의 상태가 설계서와 다르거나, 설계서에 오류·누락이 있는 경우 발주기관에 서면으로 설계변경을 요청할 수 있습니다. 다만, 최종 설계변경 승인 권한은 발주기관에 있습니다.
- 근거: 시행령 제65조 제1호~제3호
- 계약상대자는 현장 상태를 입증할 자료(사진, 시험 성적서 등) 첨부 필요

---

### Q. 설계변경 없이 시공한 부분도 정산할 수 있는가?

**A:** **원칙적으로 불가합니다.** 설계변경은 반드시 **사전 승인** 후 시공해야 합니다. 승인 없이 임의로 변경·시공한 부분은 계약금액 조정 대상이 아니며, 감사 지적 사유가 됩니다. 다만, 긴급한 사유(안전사고 우려 등)로 사전 승인이 불가능한 경우에는 사후 승인이 인정될 수 있으나, 긴급성에 대한 소명이 필요합니다.
- 근거: 시행령 제65조, 계약집행기준

---

### Q. 신규비목 단가 산정 시 계약상대자와 협의가 필요한가?

**A:** **네.** 신규비목은 당초 계약에 없는 항목이므로, 발주기관이 산정한 단가에 대해 계약상대자와 **협의**를 거쳐야 합니다. 협의가 성립하지 않으면 발주기관이 산정한 단가를 적용하되, 계약상대자는 이의를 제기할 수 있습니다.
- 산정 순서: ① 설계변경 당시 단가 → ② 거래실례가격 → ③ 감정가격
- 근거: 시행령 제66조

---

### Q. 설계변경으로 총공사비가 10%를 초과하면 어떻게 하나?

**A:** 원칙적으로 설계변경에 의한 공사량 증가는 **총공사비의 10% 이내**로 제한됩니다. 10%를 초과하는 경우에는 초과 부분에 대해 **별도 계약**(분리 발주) 또는 추가 예산 확보 후 **기관장 승인**을 받아야 합니다. 발주기관의 필요에 의한 설계변경은 예외적으로 10%를 초과할 수 있으나, 그 사유를 명확히 소명해야 합니다.
- 근거: 시행령 제65조의2
