### Q. 계약보증금을 분할 납부할 수 있나?

**A:** 원칙적으로 **일시 납부**해야 합니다. 다만, 장기계속계약의 경우 각 연차별 계약금액에 대한 계약보증금을 해당 연차 계약 체결 시 납부할 수 있습니다.
- 근거: 시행령 제37조
- 장기계속계약은 연차별 계약이므로 해당 연차분만 납부

---

### Q. 보증보험증권과 공제조합 보증서 중 어느 것을 요구해야 하나?

**A:** 계약상대자가 **자유롭게 선택**할 수 있습니다. 발주기관이 특정 보증수단만 요구하는 것은 부당합니다. 시행령에 열거된 보증수단 중 계약상대자가 선택하여 납부합니다.

---

### Q. 계약보증금이 면제된 경우에도 불이행 시 징수가 가능한가?

**A:** **네, 가능합니다.** 계약보증금을 면제한 경우에도 계약불이행 시에는 계약금액의 **100분의 10에 해당하는 금액**을 징수할 수 있습니다.
