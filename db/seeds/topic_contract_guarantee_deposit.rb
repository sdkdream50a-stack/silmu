## 지방계약법 제15조 (계약보증금)

① 지방자치단체의 장 또는 계약담당자는 계약을 체결하는 경우에는 계약상대자로 하여금 계약금액의 **100분의 10 이상의 계약보증금**을 납부하게 하여야 한다.

② 계약상대자가 계약을 이행하지 아니하는 때에는 해당 계약보증금을 **해당 지방자치단체에 귀속**시켜야 한다. 다만, 연대보증인이 있는 경우에는 대통령령으로 정하는 바에 따른다.

③ 제1항에 따른 계약보증금의 납부 및 귀속 등에 관한 사항은 **대통령령으로 정한다.**

### 보증금의 법률적 위치

- **목적:** 계약상대자의 계약 이행을 담보하기 위한 금전적 보장수단
- **원칙:** 계약금액의 10% 이상 납부 의무
- **귀속:** 계약 불이행 시 해당 지방자치단체에 귀속
- **위임:** 납부 면제, 납부 방법, 귀속 절차 등 세부사항은 시행령에 위임

> 계약보증금은 계약 이행을 확보하기 위한 핵심 제도로, 납부 의무와 귀속은 법률에서 직접 규정

### 제12조 (입찰보증금)

① 경쟁입찰에 참가하려는 자는 입찰금액의 **100분의 5 이상**의 입찰보증금을 납부하여야 한다.

② 낙찰자가 계약을 체결하지 아니하는 때에는 해당 입찰보증금을 **해당 지방자치단체에 귀속**시켜야 한다.

### 관련 조항

- **제12조 (입찰보증금):** 입찰참가 시 입찰금액의 5% 이상 납부
- **제21조 (하자보증):** 공사 등의 하자보수를 담보하기 위한 하자보증금 납부 의무
