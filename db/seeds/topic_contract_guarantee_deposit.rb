| 귀속사유 | 낙찰자가 **계약 미체결** 시 귀속 |
| 면제 | 국가·지자체 등 공공기관, 소액수의계약 등 |

### 제51조 (하자보증금)

- 하자보증금은 **공종별로 계약금액의 2~5%** 차등 납부
- 하자보증기간 종료 시까지 보관
- 하자 미발생 시 기간 만료 후 반환

| 공종 구분 | 하자보증금률 |
