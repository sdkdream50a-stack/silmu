| **완료검사** | 용역 완료 시 | 과업지시서 대비 이행 내용 확인 |

### 제66조 (검사조서)

① 검사를 완료한 때에는 **검사조서를 작성**하여야 한다.

② 검사조서에 포함할 사항:
- 계약 건명 및 계약번호
- 계약금액 및 이행 내용
- 검사일시 및 검사자
- **합격·불합격 판정** 및 사유
- 시정 요구사항 (불합격 시)

③ 다만, 추정가격 **5천만원 이하**인 계약은 검사조서 작성을 생략할 수 있다.

### 제67조 (기성검사)

- 장기계속공사·계속비공사에서 **기성부분에 대한 검사**
- 기성 검사 후 **기성대가** 지급
- 기성률에 따라 검사 실시 (월별 또는 공정률 기준)
- 기성검사 시에도 **검사조서 작성** 필요
