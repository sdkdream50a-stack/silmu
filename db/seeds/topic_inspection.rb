| 검사 구분 | 적용 대상 | 내용 |
|----------|----------|------|
| **준공검사** | 공사 완료 시 | 설계도서 대비 전체 시공 내용 확인 |
| **기성검사** | 공사 진행 중 | 기성부분에 대한 중간검사 (대가 지급용) |
