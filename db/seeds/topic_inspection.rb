## 지방계약법 제16조 (검사)

① 지방자치단체의 장 또는 계약담당자는 계약상대자가 계약의 전부 또는 일부의 이행을 완료한 때에는 **계약서·설계서 및 그 밖의 관계 서류에 따라 이를 확인하기 위하여 검사**를 하여야 한다.

② 제1항에 따른 검사는 **대통령령으로 정하는 바에 따라** 계약담당자 또는 **검사를 담당하는 공무원**이 하여야 한다.

③ 제2항에도 불구하고 전문적인 지식이나 기술이 필요한 경우에는 **전문기관에 의뢰**하여 검사를 할 수 있다.

### 검사의 법률적 위치

- **목적:** 계약 이행의 적정성을 확인하는 절차
- **의무:** 계약이행 완료 시 반드시 검사 실시 (법률상 의무)
- **주체:** 계약담당자, 검사 담당 공무원, 또는 전문기관
- **위임:** 검사 절차, 기한, 검사조서 등 세부사항은 시행령에 위임

> 검사는 대금 지급의 전제 조건으로, 검사 완료 후에야 대금 지급이 가능

### 관련 조항

- **제17조 (대가의 지급):** 검사 완료 후 대가 지급 의무 (14일 이내)
