### 제64조 (검사)

① 계약담당자는 계약상대자가 계약이행을 완료한 때에는 그 통지를 받은 날부터 **14일 이내**에 검사를 완료하여야 한다.

② 검사 시 **계약서·설계서·과업지시서** 등에 따라 해당 계약의 이행 내용을 확인

③ 전문적 검사가 필요한 경우:
- **전문기관 의뢰** 가능
