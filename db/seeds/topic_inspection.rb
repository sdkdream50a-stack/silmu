## 검사·검수 관련 유권해석

### Q. 검사 기한(14일)을 초과하면 어떻게 되나?

**A:** 이행완료 통보 후 14일 이내에 검사를 완료해야 합니다. 기한을 초과한 경우, 그로 인해 대금 지급이 지연되면 계약상대자에게 **지연이자**를 지급해야 할 수 있습니다.
- 근거: 시행령 제64조, 제68조
- 발주기관 귀책으로 지급이 지연된 것이므로 지연이자 발생

---

### Q. 검사담당자와 계약담당자가 같은 사람이어도 되나?

**A:** 소액계약(추정가격 5천만원 이하)의 경우 가능하지만, 그 이상의 계약에서는 **분리하는 것이 원칙**입니다. 내부 견제(내부통제) 기능을 위해 다른 사람을 지정하는 것이 바람직합니다.

---

### Q. 검사조서 작성을 생략할 수 있는 경우는?

**A:** 추정가격 **5천만원 이하**인 계약의 경우 검사조서 작성을 생략할 수 있습니다. 다만, 검사 사실 자체는 기록으로 남겨야 하므로 간이검사확인서 등으로 대체합니다.
- 근거: 시행령 제66조 제3항

---

### Q. 일부 불합격 시 합격 부분만 인수할 수 있나?

**A:** **가능합니다.** 분할 납품이 허용된 계약의 경우, 합격 부분만 인수하고 해당 부분의 대금을 지급할 수 있습니다. 불합격 부분은 교환·보완 후 재검사합니다.
