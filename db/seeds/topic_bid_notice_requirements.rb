  - 전자입찰 시스템 장애

  **처리 절차:**
  1. 취소·연기 사유 발생 확인
  2. 결재 (계약담당자 또는 계약담당관)
  3. 나라장터 공고 취소·변경 등록
  4. 기 납부 입찰보증금 즉시 반환
  5. 재공고 (연기의 경우 새 일정 확정 후 공고)
  </div>
