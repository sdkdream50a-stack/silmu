  1. **유의서 첨부 방법**: 나라장터(G2B) 입찰공고 등록 시 유의서 파일을 첨부하거나, 공고문 본문에 포함하여 공지
  2. **전자서명 필수**: 전자입찰 참가자는 공인인증서(공동인증서)로 전자서명 후 제출
