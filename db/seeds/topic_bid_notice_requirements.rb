  ### 전자입찰 유의서 vs 종이 유의서

  | 구분 | 전자입찰 (나라장터) | 오프라인 입찰 |
  |------|------------------|------------|
  | 유의서 교부 | G2B 첨부 파일 다운로드 | 방문·우편 교부 |
  | 확인 의무 | 입찰 참가자 전원 다운로드 확인 | 수령 서명 |
  | 수정 통지 | 정정 공고를 G2B에 게재 | 등록 참가자 전원 개별 통지 |
  | 분쟁 시 기준 | G2B 게재 내용 기준 | 교부된 서면 기준 |
