  **STEP 1 — 표준 유의서 선택**
  - 행정안전부 또는 기관 자체 표준 유의서 양식 사용
  - 공사·물품·용역 계약별 해당 양식 선택

  **STEP 2 — 계약별 특기 사항 기재**
  - 공동도급 허용 여부 및 구성원 요건
  - 현장설명회 일정 (해당 시)
  - 규격가격 분리입찰 여부 (해당 시)
  - 기술제안서 제출 여부 (협상계약)

  **STEP 3 — 입찰 무효 조항 명확화**
  - 법령상 무효 사유 + 공고별 특수 무효 조건 명시
  - 낙찰하한율, 적격심사 합격 기준 등 수치 명기

  **STEP 4 — 공고 첨부 방법**

  | 방법 | 내용 |
  |------|------|
  | 나라장터 첨부 파일 | 유의서 PDF/HWP 파일 업로드 |
  | 공고문 본문 포함 | 유의서 내용을 공고 본문에 직접 기재 |
  | 우편·방문 교부 | 2천만원 이하 오프라인 입찰 시 |

  ### 유의서 내용 오류 발생 시 조치

  - **경미한 오류**: 정정 공고를 통해 수정 내용 공지 (입찰 참가자 전원에게 동일 공지)
  - **중대한 오류**: 입찰 취소 후 재공고 (내용 오류로 인한 취소는 기 납부 보증금 전액 반환)
