## 4. 행정 처리 오류 환수 — 단순 환수, 5년 시효

본인의 부정 신고가 아닌 학교 행정실의 처리 오류로 과다 지급된 경우라도 환수 대상입니다. 다만 **부정 수령이 아니므로 5배 가산은 적용되지 않고, 과다 지급액만 단순 환수**됩니다(시간외수당 §15⑧의 5배 가산 조항과 비교 시 차이). 5년 시효 내에 환수가 가능하므로, 학교 행정실은 호봉 산정 결정서·증빙 서류를 5년 이상 보존해야 합니다.

본인이 잘못 신고한 경우(예: 자신의 사기업 경력을 공무원 경력으로 거짓 기재)는 「국가공무원법」 §47 ③에 따라 5배 가산 환수 + 징계 사유가 됩니다.

