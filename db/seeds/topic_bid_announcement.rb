## 입찰공고 관련 유권해석

### Q. 공고기간 산정 시 공휴일을 제외하나?

**A:** **아닙니다.** 역일(曆日) 기준이므로 토·일·공휴일 모두 포함합니다. 다만 입찰 마감일이 공휴일인 경우 그 다음 영업일로 자동 연장됩니다.

---

### Q. 공고 후 규격서를 수정해도 되나?

**A:** 가능하지만, **변경공고**를 해야 합니다. 변경 내용에 따라 공고기간을 재산정해야 하며, 이미 참가 등록한 업체에 변경사항을 개별 통지해야 합니다.

---

### Q. 긴급공고(5일) 사유로 '예산 집행 기한'이 인정되나?

**A:** **원칙적으로 불인정합니다.** 긴급공고는 천재지변, 전시, 국가안보 등 불가피한 사유에 한정됩니다. 단순히 연말 예산 집행을 위한 기간 단축은 감사 지적 대상입니다.

---

### Q. 재공고 시 참가자격 완화가 가능한가?

**A:** 재공고 시 입찰조건은 최초 공고와 **동일**해야 합니다. 다만, 유찰 원인이 과도한 참가자격 제한인 경우, 참가자격을 완화하여 **새로운 공고**(재공고 아닌 신규공고)로 진행할 수 있습니다.
