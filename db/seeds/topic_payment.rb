## 지방계약법 시행령 — 대금지급 관련 조항

### 제68조 (대가의 지급)

① 계약담당자는 검사를 완료한 후 계약상대자로부터 대가 지급 청구를 받은 때에는 청구를 받은 날부터 **5일 이내**에 대가를 지급하여야 한다.

② 다만, 계약상대자의 청구 내용의 전부 또는 일부가 부당함을 발견한 때에는 **그 사유를 명시하여 계약상대자에게 통지**하고 부당한 부분을 제외한 나머지 금액을 기한 내에 지급하여야 한다.

③ 대가 지급이 지연된 경우 **지연일수에 대한 이자**를 지급하여야 한다.

### 대가 지급 기한 요약

| 구분 | 기한 | 기산일 |
|------|------|--------|
| **대가 지급** | **5일 이내** | 대금 지급 청구를 받은 날 |
| **검사 완료** | **14일 이내** | 이행완료 통보를 받은 날 |
| **전체 소요기한** | **최대 19일** | 이행완료 통보 ~ 대금 지급 |

### 제53조 (선금의 지급)

① 선금은 계약금액의 **100분의 70 이내**에서 지급할 수 있다.

② 선금 지급 기준:

| 계약 유형 | 선금률 |
|----------|--------|
| **공사** | 계약금액의 **70% 이내** |
| **물품 제조·구매** | 계약금액의 **70% 이내** |
| **용역** | 계약금액의 **70% 이내** |

③ 선금을 지급받은 자는 그 선금을 다음 각 호의 용도로 사용하여야 한다:
- 계약 목적물의 제조·구매에 필요한 **자재비·노무비** 등
- **하도급대금** 지급

④ 선금을 지급받은 자는 **선금 사용 내역**을 발주기관에 제출하여야 한다.

### 제54조 (기성대가 및 기납대가의 지급)

① **기성부분** 또는 **기납부분**에 대하여 검사를 완료한 경우, 계약상대자의 청구에 따라 그 대가를 지급하여야 한다.

② 기성대가의 산정:
- 기성검사 합격 부분에 대해 **계약 단가** 적용
- 선금 지급분은 기성비율에 따라 **선금 공제** 후 지급
- 기성대가 = (계약 단가 x 기성물량) - 선금공제액

③ 기성검사 주기:
- **월별 기성:** 매월 시공 또는 이행 부분에 대해 검사
- **공정률 기성:** 일정 공정률 도달 시 검사

### 제55조 (준공대가의 지급)

① 준공검사를 완료한 경우 계약상대자의 청구에 따라 **준공대가**를 지급하여야 한다.

② 준공대가 산정:
- 총 계약금액에서 기 지급한 **선금·기성대가**를 공제
- 지체상금이 있는 경우 **지체상금 공제** 후 지급

### 제68조의2 (지연이자)

대가 지급이 지연된 경우 지연이자를 지급하여야 한다:

| 항목 | 내용 |
|------|------|
| **이자율** | 지방계약법 시행령에서 정하는 이율 |
| **기산일** | 지급 기한의 다음 날 |
| **종료일** | 실제 지급일 |
| **산정** | 미지급 금액 x 이자율 x 지연일수/365 |
