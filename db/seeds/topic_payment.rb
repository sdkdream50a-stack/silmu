### Q3. 기성대가에서 선금은 어떻게 공제하나요?

**A:** 기성비율에 따라 비례 공제합니다.
- **공식:** 선금공제액 = 선금액 x (당해 기성액 / 계약금액)
- **예시:** 계약금액 10억, 선금 3억(30%), 기성 4억인 경우
  - 선금공제 = 3억 x (4억/10억) = **1.2억원**
  - 기성대가 = 4억 - 1.2억 = **2.8억원**

---

### Q4. 대금 지급이 지연되면 이자를 받을 수 있나요?

