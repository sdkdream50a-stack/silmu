## 대금지급 관련 유권해석

### Q. 검사 완료 전에 대가를 지급할 수 있는가?

**A:** **원칙적으로 불가합니다.** 대가 지급은 검사 완료를 전제로 하며, 검사를 거치지 않고 대금을 지급하면 위법입니다. 다만, 계약 특약으로 검사와 동시에 대금을 지급하는 것은 가능합니다.
- 근거: 지방계약법 제16조의2, 시행령 제68조
- 검사 완료 = 대금 지급의 법적 전제 조건

---

### Q. 대금 지급 기한 5일은 영업일 기준인가?

**A:** **역일(달력일) 기준**입니다. 공휴일이나 토·일요일도 포함하여 산정합니다. 다만, 기한의 말일이 공휴일인 경우에는 그 다음 영업일까지 연장됩니다.
- 근거: 민법 제161조 (기한 말일이 공휴일인 경우)
- 실무상 공휴일이 끼어 있으면 실질적으로 영업일 기준에 가까워질 수 있음

---

### Q. 선금을 타 계약에 전용하면 어떻게 되나?

**A:** **위법입니다.** 선금은 해당 계약의 이행 목적으로만 사용해야 하며, 타 계약이나 다른 용도로 사용하면 선금 반환 청구 사유가 됩니다. 심한 경우 부정당업자 제재 대상이 될 수 있습니다.
- 근거: 지방계약법 제19조, 시행령 제53조
- 선금 사용 내역 제출 의무 위반에도 해당

---

### Q. 하도급대금 직접지급 요청 시 반드시 직접지급해야 하나?

**A:** 하도급법상 직접지급 사유에 해당하면 발주기관은 **직접지급 의무**가 있습니다. 하수급인이 정당하게 요청하고, 하도급계약 내용이 확인되면 수급인의 동의 없이도 직접 지급할 수 있습니다.
- 근거: 하도급거래 공정화에 관한 법률 제14조
- 직접지급 시 수급인에게 통보 필요
