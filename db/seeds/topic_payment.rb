③ 선금의 지급 시기·방법·한도 및 선금의 사용·정산 등에 필요한 사항은 **대통령령으로 정한다.**

### 선금의 법률적 위치

- **목적:** 계약상대자의 자금 부담 경감 및 원활한 계약이행 촉진
- **성격:** 계약대금의 선급(미리 지급하는 금액)
- **사용 제한:** 반드시 해당 계약 이행 목적으로만 사용
