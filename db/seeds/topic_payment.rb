## 지방계약법 제18조 (대가의 지급)

① 지방자치단체의 장 또는 계약담당자는 검사를 완료한 후 계약상대자가 대가의 지급을 청구한 때에는 그 청구를 받은 날부터 **5일 이내**에 대가를 지급하여야 한다.

② 제1항에도 불구하고 대가를 지급할 수 없는 경우에는 그 사유를 계약상대자에게 통지하여야 하며, **대통령령으로 정하는 바에 따라** 지연 지급에 대한 이자를 지급하여야 한다.

