## 계약심사 관련 유권해석

### Q. 계약심사를 생략하고 계약을 체결한 경우 계약의 효력은?

**A:** 계약심사를 받지 않고 체결한 계약은 **계약 자체의 효력에는 영향이 없습니다.** 다만, 담당자에 대한 **행정상 책임(징계 등)**이 부과될 수 있으며, 감사 시 지적 사항이 됩니다. 지방계약법은 계약심사를 계약 유효 요건으로 규정하지 않으므로 계약 자체는 유효하나, 절차 위반으로 처분받을 수 있습니다.

