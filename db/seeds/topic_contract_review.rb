### 자주 묻는 질문 (FAQ)

#### Q1. 계약심사와 계약 검토의 차이는 무엇인가요?
**A:** 계약심사는 **지방계약법 제9조의2**에 근거한 **법적 의무 절차**로, 지정된 심사 부서(또는 위원회)가 예정가격·계약방법·계약조건 등을 공식적으로 검토합니다. 반면, 계약 검토는 특정 법령 근거 없이 담당자나 부서 내에서 자체적으로 점검하는 비공식 절차입니다. 심사 결과는 문서로 기록·보존해야 하는 점도 다릅니다.

#### Q2. 계약심사 면제 신청은 어떻게 하나요?
**A:** 긴급 사유 등 면제 사유가 발생한 경우, **계약심사 면제 신청서**에 면제 사유를 구체적으로 기술하고 관련 증빙서류를 첨부하여 계약심사 부서에 제출합니다. 면제 승인 후 계약심사 없이 계약을 진행할 수 있으며, 면제 결정 서류는 계약서류와 함께 편철합니다.

#### Q3. 계약심사 결과를 반드시 따라야 하나요?
**A:** 법적으로 심사 의견에 **강제적 구속력은 없습니다.** 담당 부서가 합리적인 사유를 제시하고 결재권자의 승인을 받으면 미수용 처리가 가능합니다. 다만, 감사 시 심사 의견을 무시한 근거를 소명해야 하므로, 실무적으로는 심사 의견을 최대한 반영하는 것이 안전합니다.

#### Q4. 자체 계약심사와 외부 계약심사(조달청 의뢰)의 차이는?
**A:** **자체 계약심사**는 지방자치단체 내부 심사 부서나 계약심사위원회가 심사하며 신속한 처리가 가능합니다. **외부 계약심사(조달청 등 전문기관 의뢰)**는 전문기관이 심사하므로 객관성이 높고, 전문기관의 심사 결과를 받으면 자체 심사를 생략할 수 있습니다. 대형 공사(예: 10억원 이상)나 전문성이 요구되는 계약에는 외부 심사를 활용하는 것이 유리합니다.
