① 지방자치단체의 장 또는 계약담당자는 공사·물품·용역 그 밖의 계약에 있어 필요하다고 인정하는 경우에는 계약상대자를 **2인 이상으로 하는 공동계약**을 체결할 수 있다.

② 지방자치단체의 장 또는 계약담당자는 제1항에 따른 공동계약의 경우 입찰 참가자격으로 **지역을 제한하지 아니하는 입찰**로서 건설업 등의 **균형발전**을 위하여 필요하다고 인정할 때에는 공사현장을 관할하는 특별시·광역시·특별자치시·도 및 특별자치도에 **법인등기부상 본점소재지**가 있는 자 **1인 이상**을 공동수급체의 구성원으로 하여야 한다.

