---

### Q. 입찰 후 공동수급체 구성원을 변경할 수 있나?

**A:** **원칙적으로 불가**합니다. 공동수급체는 입찰 전에 구성해야 하며, 입찰 후 구성원 변경은 허용되지 않습니다. 다만, 계약 이행 중 구성원의 부도·폐업 등 **불가피한 사유**가 발생한 경우, 발주기관의 승인을 받아 구성원 교체가 가능합니다.
- 근거: 지방자치단체 입찰 및 계약집행기준
