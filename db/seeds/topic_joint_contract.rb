## 지방계약법 시행령 제88조 (공동계약)

① 지방자치단체의 장 또는 계약담당자는 법 제29조제1항에 따른 공동계약을 체결하려는 경우에는 공동수급체의 구성원으로 하여금 **공동으로 이행하게 하거나 분담하여 이행**하게 할 수 있다.

② 제1항의 경우 **공동도급의 유형**, 공동수급체 구성원 상호간의 시공상 **책임한계** 등 공동계약에 필요한 세부사항은 **행정안전부장관**이 정한다.

③ 지방자치단체의 장 또는 계약담당자가 입찰에 의하여 계약을 체결하려는 경우에는 계약의 목적과 성질상 공동계약으로 하는 것이 **부적절하다고 인정되는 경우를 제외**하고는 가능하면 공동계약으로 할 수 있다.

### 공동도급의 유형 비교

| 구분 | 공동이행방식 | 분담이행방식 | 혼합방식 |
|------|------------|------------|---------|
| **정의** | 자금·인력 등을 공동 출자·파견하여 이행 | 공종별로 분담하여 각자 이행 | 공동이행 + 분담이행 혼합 |
| **이익/손실** | 출자비율에 따라 배당·분담 | 각자 분담 부분에 대해 독립 | 방식별 적용 |
| **책임** | 연대 책임 (원칙) | 분담 부분에 대해 각자 책임 | 방식별 적용 |
| **보증** | 연대보증 | 분담보증 가능 | 혼합 적용 |
| **적합 유형** | 종합건설 공사 | 전문공종 분리 가능 공사 | 대형 복합 공사 |

### 공동수급체 구성 기준

| 항목 | 기준 |
|------|------|
| 최소 구성원 수 | **2인 이상** |
| 구성원별 최소 지분율 | **5% 이상** (원칙) |
| 지분율 조정 범위 | 4%~6% (20% 범위 내 조정 가능) |
| 대표사 최소 지분율 | 공동이행: **30% 이상** (원칙) |
| 자격요건 | 해당 업종의 면허·허가·등록 등 보유 필수 |
| 구성 시기 | **입찰 전** (현장설명 의무 시 현장설명일 전) |
| 구성 변경 | 입찰 후 구성원 변경 **불가** (원칙) |

