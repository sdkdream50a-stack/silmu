### 공동이행방식 vs 분담이행방식 선택 기준

**공동이행방식이 적합한 경우:**
- 공종 분리가 어려운 **종합건설 공사**
- 구성원 간 **긴밀한 협력**이 필요한 공사
- 중소기업 공동도급으로 **시공능력 보완**이 필요한 경우

**분담이행방식이 적합한 경우:**
- 공종이 **명확히 구분**되는 복합 공사
- 각 공종별 **전문업체**가 독립적으로 이행 가능한 경우
- 건축+토목, 건축+설비 등 **이종업종 결합** 공사

