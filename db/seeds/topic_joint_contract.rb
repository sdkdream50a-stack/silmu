### 분담이행방식 운영 기준

- 구성원이 **공종별로 분담**하여 독립적으로 이행
- 각 구성원은 **분담 부분에 대해 직접 책임**
- 분담 내용은 공동수급협정서에 **구체적으로 명시**
- 분담 부분의 하도급은 해당 구성원이 단독 결정

### 혼합방식 허용 요건

- 공동이행과 분담이행을 혼합하지 않으면 **입찰진행이 곤란**한 경우
- 계약목적 달성이 **사실상 곤란**한 경우에 한정
- 공동이행 참여 구성원이 분담이행으로 중복 참여 **불가** (원칙)
