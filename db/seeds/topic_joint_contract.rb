## 공동도급 관련 유권해석

### Q. 공동수급체 구성원 중 1인이 부도가 나면 나머지 구성원이 이행해야 하나?

**A:** **공동이행방식**의 경우, 구성원 전원이 **연대 책임**을 부담하므로 나머지 구성원이 해당 부분까지 이행해야 합니다. **분담이행방식**의 경우에는 부도 업체의 분담 부분에 대해서는 발주기관과 나머지 구성원이 협의하여 처리합니다.
- 근거: 지방계약법 시행령 제88조, 지방자치단체 입찰 및 계약집행기준
