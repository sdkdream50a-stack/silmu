## 분할계약 질의답변

### Q1. 분할계약이란 무엇이고 왜 금지되나요?

**A:** 분할계약이란 **동일한 목적의 물품·공사·용역 계약을 여러 건으로 나누어** 각각 수의계약 기준금액 이하로 계약하는 행위입니다. 경쟁입찰을 회피하여 특정 업체와 수의계약하는 것을 방지하고, 예산집행의 투명성을 확보하기 위해 법으로 금지하고 있습니다.
- 금지 근거: 지방계약법 시행령 제25조, 제7조

---

### Q2. 분할계약에 해당하는지 어떻게 판단하나요?

**A:** 다음 5가지 기준을 종합적으로 판단합니다.
- **동일 목적·용도** 여부
- **동일 또는 유사 규격** 여부
- **동일 예산과목** 여부
- **동일 시기·장소** 여부
- **합산 추정가격**이 수의계약 기준 초과 여부
5가지 중 다수가 해당하면 분할계약으로 판단될 가능성이 높습니다.

---

### Q3. 분할계약이 적발되면 어떤 제재를 받나요?

**A:** 담당자는 경고·정직 등 징계를 받을 수 있으며, 이미 체결된 계약은 취소되고 재입찰이 명령될 수 있습니다. 부당 지급된 금액은 환수 대상이 될 수 있으며, 관여한 업체는 부정당업자로 제재받아 일정 기간 입찰 참가 자격이 제한될 수 있습니다.

