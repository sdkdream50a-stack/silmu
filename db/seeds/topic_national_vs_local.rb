## 국가계약법 vs 지방계약법 — 법률 기본 비교

### 적용 대상 및 기본 구조

| 구분 | 국가계약법 | 지방계약법 |
|------|----------|----------|
| **정식 명칭** | 국가를 당사자로 하는 계약에 관한 법률 | 지방자치단체를 당사자로 하는 계약에 관한 법률 |
| **적용 기관** | 중앙행정기관, 헌법기관, 국가기관 | 지방자치단체 (시·도, 시·군·구 등) |
| **주무 부처** | 기획재정부 | 행정안전부 |
| **관련 예규** | 기획재정부 계약예규 (조달청 고시 포함) | 지방자치단체 입찰 및 계약집행기준 |

### 계약 원칙 비교

**국가계약법 제5조 (계약의 원칙):**
> 각 중앙관서의 장 또는 계약담당공무원은 이 법에 의한 계약을 체결함에 있어서 계약 당사자 간에 대등한 입장에서 공정하게 계약이 체결되도록 하여야 하며, 계약 당사자는 계약의 내용을 신의성실의 원칙에 따라 이행하여야 한다. 각 중앙관서의 장은 부당한 특약 또는 조건을 정해서는 아니 된다.

**지방계약법 제6조 (계약의 원칙):**
> 지방자치단체의 장 또는 계약담당자는 이 법에 따른 계약을 체결함에 있어서 계약 당사자 간에 대등한 입장에서 공정하게 계약이 체결되도록 하여야 하며, 계약 당사자는 계약의 내용을 신의성실의 원칙에 따라 이행하여야 한다. 지방자치단체의 장은 부당한 특약 또는 조건을 정해서는 아니 된다.

두 법률의 계약 원칙은 **신의성실·대등한 입장·공정한 계약·부당특약 금지** 면에서 동일한 취지입니다.

### 조달사업법 및 전자조달법

- **조달사업에 관한 법률 (조달사업법):** 조달청의 중앙조달 업무 근거법. 국가기관의 물자 조달·시설공사 계약·비축물자 관리를 규율.
- **전자조달의 이용 및 촉진에 관한 법률 (전자조달법):** 나라장터(KONEPS) 등 전자조달시스템 운영의 법적 근거.
