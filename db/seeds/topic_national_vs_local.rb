## 자주 묻는 질문 (Q&A)

### Q. 공기업·준정부기관은 어느 법을 적용하나요?

**A:** 공기업·준정부기관은 「공기업·준정부기관 계약사무규칙」을 따르며, 국가계약법·지방계약법과 구별됩니다. 다만 기본 원칙(경쟁 입찰, 신의성실 등)은 유사합니다.

---

### Q. 지방계약법에서 "지연배상금"이라는 용어를 쓰는 이유는?

**A:** 국가계약법은 "지체상금"이라는 용어를 사용하고(시행령 제74조), 지방계약법은 "지연배상금"이라는 용어를 사용합니다(시행령 제90조). 두 용어는 같은 개념(납기 지연 시 계약금액에서 차감하는 손해배상금)이나 용어가 다릅니다. 시험에서는 어느 법률의 규정인지 문맥으로 파악하세요.

---

### Q. 공사 지체상금률이 국가(1/1,000)와 지방(0.5/1,000)이 다른 이유는?

**A:** 주무부처(기획재정부 vs 행정안전부)가 다르고, 각 법률이 적용되는 기관의 특성을 반영하여 별도로 책정되어 있습니다. 국가계약법의 요율이 지방계약법보다 2배 높습니다. 시험에서 숫자를 혼동하지 않도록 주의하세요.

---

### Q. 분할계약 금지가 두 법률에서 조문 번호가 다른가요?

**A:** 네. 국가계약법 시행령 제68조는 "공사의 분할계약금지"를 규정하고, 지방계약법 시행령 제77조도 동일한 분할계약 금지 원칙을 규정합니다. 취지는 같으나 조문 번호가 다릅니다.

---

### Q. 수의계약 금액 기준은 두 법률이 같나요?

**A:** 네. 물품·용역 2,000만원 이하, 공사 종합 4억원·전문 2억원·기타 1.6억원 이하 등 수의계약 금액 기준은 국가계약법과 지방계약법 모두 동일합니다.
