    QUESTIONS.select { |q| q[:subject_id] == subject_id.to_i }
  end

  # 전체 문제 (무작위 순서)
  def self.all_random
    QUESTIONS.shuffle
  end

  # 전체 문제 (순서 유지)
  def self.all
    QUESTIONS
  end

  # 총 문제 수
  def self.count
    QUESTIONS.size
  end

  # 과목별 문제 수
  def self.count_by_subject(subject_id)
    by_subject(subject_id).size
  end
end
